import fs from "fs";
import { build, defaultOptions } from "./common";
import { ArgumentParser } from "argparse";

function log(...args: any[]) {
    console.debug(...args);
}

function getArgs() {
    const parser = new ArgumentParser({
        description: "Build autogenerated data entry form for DHIS2 data sets",
    });

    parser.add_argument("--skip-build", {
        help: "Skip build process",
        dest: "skipBuild",
        action: "store_true",
    });

    return parser.parse_args() as { skipBuild: boolean };
}

export function convertReactHtmlToInline() {
    const args = getArgs();

    if (!args.skipBuild) build("autogenerated-forms");

    log(`Read: build/index.html`);
    const html = fs.readFileSync("build/index.html", "utf8");
    const head = getSection(html, "head");
    const fragment = [getTags(html, "style"), getHtmlBodyContents(html), getTags(head, "script")].join("\n");

    fs.mkdirSync("dist", { recursive: true });
    const dest = defaultOptions.autogenFormOutput;
    log(`Write: ${dest}`);
    const { version } = JSON.parse(fs.readFileSync("package.json", "utf8")) as { version: string };
    const markedHtml = `<!-- d2-autogen-forms-custom-form v${version} -->\n${fragment}`;
    fs.writeFileSync(dest, markedHtml);
}

function getTags(html: string, tag: "script" | "style"): string {
    const matches = html.match(new RegExp(`<${tag}[\\s\\S]*?</${tag}>`, "g")) ?? [];
    return matches.join("\n");
}

function getSection(html: string, tag: "head" | "body"): string {
    const startTag = `<${tag}>`;
    const endTag = `</${tag}>`;
    return html.slice(html.indexOf(startTag) + startTag.length, html.indexOf(endTag));
}

function getHtmlBodyContents(html: string): string {
    return getSection(html, "body");
}

function main() {
    convertReactHtmlToInline();
}

main();
