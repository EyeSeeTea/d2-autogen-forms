/**
 * Integration script: verifies that installing a custom form does not lose any
 * dataset metadata.
 *
 * Usage:
 *   DHIS2_BASE_URL=https://play.im.dhis2.org/stable-2-42-5 \
 *   DHIS2_USERNAME=system \
 *   DHIS2_PASSWORD=System123 \
 *   DATASET_ID=lyLU2wR22tC \
 *   npx ts-node -P tsconfig-cli.json src/scripts/verify-form-install.ts
 */

import { D2Api } from "../types/d2-api";
import { Dhis2CustomFormRepository } from "../data/autogenerated-forms-configurator/Dhis2CustomFormRepository";

// Fields whose change is expected and should not be flagged
const ALLOWED_CHANGES = new Set(["lastUpdated", "dataEntryForm", "formType", "version"]);

// Sample HTML to install
const SAMPLE_HTML = "<div>d2-autogenforms verification form</div>";

function getEnv(name: string): string {
    const val = process.env[name];
    if (!val) throw new Error(`Missing required env var: ${name}`);
    return val;
}

function flatDiff(before: Record<string, unknown>, after: Record<string, unknown>, path = ""): string[] {
    const diffs: string[] = [];
    const allKeys = new Set([...Object.keys(before), ...Object.keys(after)]);

    for (const key of allKeys) {
        const fullPath = path ? `${path}.${key}` : key;
        if (ALLOWED_CHANGES.has(key)) continue;

        if (!(key in before)) {
            diffs.push(`ADDED: ${fullPath} = ${JSON.stringify(after[key])?.slice(0, 120)}`);
        } else if (!(key in after)) {
            diffs.push(`REMOVED: ${fullPath} (was ${JSON.stringify(before[key])?.slice(0, 120)})`);
        } else if (
            typeof before[key] === "object" &&
            before[key] !== null &&
            !Array.isArray(before[key]) &&
            typeof after[key] === "object" &&
            after[key] !== null &&
            !Array.isArray(after[key])
        ) {
            diffs.push(
                ...flatDiff(before[key] as Record<string, unknown>, after[key] as Record<string, unknown>, fullPath)
            );
        } else if (JSON.stringify(before[key]) !== JSON.stringify(after[key])) {
            const bStr = JSON.stringify(before[key]);
            const aStr = JSON.stringify(after[key]);
            const bSummary = Array.isArray(before[key])
                ? `[${(before[key] as unknown[]).length} items]`
                : bStr?.slice(0, 80);
            const aSummary = Array.isArray(after[key])
                ? `[${(after[key] as unknown[]).length} items]`
                : aStr?.slice(0, 80);
            diffs.push(`CHANGED: ${fullPath}\n  before: ${bSummary}\n  after:  ${aSummary}`);
        }
    }

    return diffs;
}

async function fetchDataSet(api: D2Api, dataSetId: string): Promise<Record<string, unknown>> {
    const response = await api
        .get<Record<string, unknown>>(`/dataSets/${dataSetId}`, {
            fields: "*",
        })
        .getData();
    return response;
}

async function main() {
    const baseUrl = getEnv("DHIS2_BASE_URL");
    const username = getEnv("DHIS2_USERNAME");
    const password = getEnv("DHIS2_PASSWORD");
    const dataSetId = getEnv("DATASET_ID");

    const api = new D2Api({ baseUrl, auth: { username, password } });
    const repo = new Dhis2CustomFormRepository(api);

    console.log(`\nVerifying form install on dataset ${dataSetId} at ${baseUrl}\n`);

    console.log("1. Fetching dataset metadata before install...");
    const before = await fetchDataSet(api, dataSetId);
    console.log(`   Got snapshot: ${Object.keys(before).length} top-level fields`);

    console.log("2. Installing custom form...");
    await repo.install(dataSetId, SAMPLE_HTML);
    console.log("   Install complete");

    console.log("3. Fetching dataset metadata after install...");
    const after = await fetchDataSet(api, dataSetId);
    console.log(`   Got snapshot: ${Object.keys(after).length} top-level fields`);

    console.log("\n4. Diffing snapshots (allowed changes excluded)...\n");
    const diffs = flatDiff(before, after);

    if (diffs.length === 0) {
        console.log("✓ PASS: No unexpected changes detected.");
        process.exit(0);
    } else {
        console.error(`✗ FAIL: ${diffs.length} unexpected change(s) found:\n`);
        for (const diff of diffs) {
            console.error(`  ${diff}`);
        }
        process.exit(1);
    }
}

main().catch(err => {
    console.error("Unexpected error:", err);
    process.exit(1);
});
