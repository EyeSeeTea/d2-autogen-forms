import { useEffect, useMemo, useState } from "react";
import { useAppContext } from "../../../contexts/app-context";
import { AutogenConfigDataSets } from "../../../../domain/autogenerated-forms-configurator/usecases/GetDataSetsUseCase";
import { CodedRef } from "../../../../domain/common/entities/Base";

type FiltersState = {
    dataSets: Option[];
    newDataSets: Option[];
};

export type Option = {
    value: string;
    text: string;
};

export function useFilters(): FiltersState {
    const { compositionRoot } = useAppContext();
    const [dataSets, setDataSets] = useState<AutogenConfigDataSets>({
        dataSetsWithExistingConfig: [],
        dataSetsWithoutConfig: [],
    });

    useEffect(() => {
        compositionRoot.dataStoreConfig.getDataSets().then(dataSets => setDataSets(dataSets));
    }, [compositionRoot.dataStoreConfig]);

    const dataSetItems = useMemoOptionsFromNamedRef(dataSets.dataSetsWithExistingConfig);
    const newDataSetItems = useMemoOptionsFromNamedRef(dataSets.dataSetsWithoutConfig);

    return { dataSets: dataSetItems, newDataSets: newDataSetItems };
}

function useMemoOptionsFromNamedRef(options: CodedRef[]): Option[] {
    return useMemo(() => options.map(option => ({ value: option.code, text: option.name })), [options]);
}
