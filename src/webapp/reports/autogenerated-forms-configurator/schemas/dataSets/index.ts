import { defaultObjectProperties, mergeArrayWithSchema, textSchema } from "..";
import { DataElementSchema } from "../../../../../domain/autogenerated-forms-configurator/entities/DataElement";
import { sectionSchema } from "./section";

export const viewTypes = [
    "table",
    "grid",
    "grid-with-totals",
    "grid-with-combos",
    "grid-with-cat-option-combos",
    "grid-with-periods",
    "grid-with-subnational-ous",
];

export const getDataSetSchema = (
    constants: string[],
    dsCode: string,
    dataElements: DataElementSchema[],
    sectionCodes: string[]
) => {
    const deCodes = dataElements.map(dataElement => dataElement.dataElementCode);

    return defaultObjectProperties({
        properties: {
            [dsCode]: defaultObjectProperties({
                properties: {
                    sections: defaultObjectProperties({
                        properties: mergeArrayWithSchema(sectionCodes, sectionSchema(constants, deCodes)),
                    }),
                    texts: {
                        properties: {
                            footer: textSchema(constants),
                            header: textSchema(constants),
                        },
                    },
                    viewType: {
                        enum: viewTypes,
                    },
                    disableComments: {
                        type: "boolean",
                    },
                },
            }),
        },
    });
};
