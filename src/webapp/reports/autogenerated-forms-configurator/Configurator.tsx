import React from "react";
import styled from "styled-components";
import { Button } from "@material-ui/core";
import { DataSetFilter } from "./DataSetFilter";
import { Editor } from "./Editor";
import i18n from "@eyeseetea/d2-ui-components/locales";
import { useConfigurator } from "./hooks/useConfigurator";
import logo from "../../components/share/logo-eyeseetea.png";
import { ConfirmationDialog } from "@eyeseetea/d2-ui-components";
import { useBooleanState } from "../../utils/use-boolean";
import { useFilters } from "./hooks/useFilters";

const AutogeneratedFormConfigurator: React.FC = () => {
    const [isDialogOpen, { enable: openDialog, disable: closeDialog }] = useBooleanState(false);

    const {
        configValue,
        dataSetCode,
        disableSave,
        newDataSetCode,
        clearConfig,
        createNewConfig,
        saveConfig,
        updateConfigValue,
        updateDataSetCode,
        updateJsonValidity,
        updateNewDataSet,
    } = useConfigurator();
    const { dataSets: dataSetItems, newDataSets: newDataSetItems } = useFilters(dataSetCode);

    return (
        <Container>
            <StyledHeader>
                <Icon src={logo} alt="Logo" />
                <h1>D2 Autogen Configurator</h1>
            </StyledHeader>

            <StyledSection>
                <DataSetFilter dataSetItems={dataSetItems} dataSetCode={dataSetCode} onChange={updateDataSetCode} />

                <Button color="primary" variant="contained" onClick={openDialog}>
                    {i18n.t("Add new data form configuration")}
                </Button>
            </StyledSection>

            <Editor
                configValue={configValue}
                dataSetCode={dataSetCode}
                updateConfigValue={updateConfigValue}
                updateJsonValidity={updateJsonValidity}
            />

            <ButtonGroup>
                <Button color="primary" variant="contained" onClick={saveConfig} disabled={disableSave}>
                    {i18n.t("Save")}
                </Button>
                <Button onClick={clearConfig} color="default" variant="contained">
                    {i18n.t("Clear")}
                </Button>
            </ButtonGroup>

            <ConfirmationDialog
                open={isDialogOpen}
                title={i18n.t("Add new data form configuration")}
                cancelText={i18n.t("Cancel")}
                saveText={i18n.t("Create config")}
                onClose={closeDialog}
                onSave={() => {
                    createNewConfig(newDataSetCode);
                    closeDialog();
                }}
                onCancel={closeDialog}
                fullWidth
                maxWidth="md"
            >
                <Container>
                    <DataSetFilter
                        dataSetItems={newDataSetItems}
                        dataSetCode={newDataSetCode}
                        onChange={updateNewDataSet}
                    />

                    {newDataSetCode && (
                        <p>
                            {i18n.t("An empty configuration will be created for the dataset {{dataSetCode}}.", {
                                dataSetCode: newDataSetCode,
                            })}
                        </p>
                    )}
                </Container>
            </ConfirmationDialog>
        </Container>
    );
};

export default React.memo(AutogeneratedFormConfigurator);

const Container = styled.div`
    padding: 1.5rem;
    display: flex;
    flex-direction: column;
    gap: 1.5rem;
`;

const StyledHeader = styled.header`
    display: flex;
    align-items: center;
    gap: 2rem;
`;

const Icon = styled.img`
    height: 4rem;
`;

const StyledSection = styled.section`
    display: flex;
    align-items: center;
    justify-content: space-between;
`;

const ButtonGroup = styled.div`
    width: 50%;
    display: grid;
    grid-template-columns: repeat(2, minmax(0, 1fr));
    gap: 0 1rem;
`;
