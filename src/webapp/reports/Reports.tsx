import React from "react";
import AutogeneratedForm from "./autogenerated-forms/AutogeneratedForm";
import AutogeneratedFormConfigurator from "./autogenerated-forms-configurator/Configurator";
import { getVariant } from "./variants";

const widget = getVariant();

const Component: React.FC = () => {
    switch (widget) {
        case "autogenerated-forms": {
            return <AutogeneratedForm />;
        }
        case "autogenerated-forms-configurator": {
            return <AutogeneratedFormConfigurator />;
        }
        default: {
            return <AutogeneratedForm />;
        }
    }
};

function Reports() {
    return <Component />;
}

export default Reports;
