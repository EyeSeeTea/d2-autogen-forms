import _ from "lodash";
import React from "react";
import {
    DataTable,
    TableHead,
    DataTableRow,
    TableBody,
    // @ts-ignore
} from "@dhis2/ui";
import { DataFormInfo } from "./AutogeneratedForm";
import { SectionWithCategoryColumns } from "../../../domain/common/entities/DataForm";
import { DataElementItem } from "./DataElementItem";
import { makeStyles } from "@material-ui/core";
import DataTableSection from "./DataTableSection";
import { CustomDataTableCell, CustomDataTableColumnHeader, fixHeaderClasses } from "./datatables/CustomDataTables";
import { GridWithCategoryColumnsViewModel } from "./GridWithCategoryColumnsViewModel";
import { useSyncedScroll } from "./hooks/Scroll";
import { getValueAccordingType } from "./DataEntryItem";

export interface GridWithCategoryColumnsProps {
    dataFormInfo: DataFormInfo;
    section: SectionWithCategoryColumns;
}

const GridWithCategoryColumns: React.FC<GridWithCategoryColumnsProps> = props => {
    const { section, dataFormInfo } = props;

    const dataElementFilter = React.useMemo(() => {
        return dataFormInfo.metadata.dataForm.dataElements.find(
            de => de.code === section.categoryOptionFilter?.dataElementCode
        );
    }, [dataFormInfo, section.categoryOptionFilter?.dataElementCode]);

    const filterValue = React.useMemo(() => {
        if (!dataElementFilter) return "";

        const dataValue = dataFormInfo.data.values.getOrEmpty(dataElementFilter, {
            categoryOptionComboId: dataFormInfo.categoryOptionComboId,
            orgUnitId: dataFormInfo.orgUnitId,
            period: dataFormInfo.period,
        });

        const value = getValueAccordingType(dataValue);

        if (!value) return "";

        return value.toString();
    }, [dataFormInfo, dataElementFilter]);

    const grid = React.useMemo(
        () => GridWithCategoryColumnsViewModel.get(section, filterValue, section.categoryOptionFilter?.config ?? []),
        [section, filterValue]
    );

    const { wrapper1Ref, wrapper2Ref, wrapper2Width } = useSyncedScroll({ enable: section.enableTopScroll });

    const fixColumns = section.fixedHeaders;
    const fixRows = section.fixedRowNames;

    const totalCategories = _(grid.columns).sumBy(col => col.totalCategories);

    const classes = useStyles();

    const tableClasses = fixColumns ? `${classes.table} ${classes.fixedHeaders}` : classes.table;

    return (
        <DataTableSection section={grid} sectionStyles={section.styles} dataFormInfo={dataFormInfo}>
            {section.enableTopScroll && (
                <div style={{ overflowX: "scroll", overflowY: "hidden", height: "11px" }} ref={wrapper1Ref}>
                    <div style={{ height: "10px", width: `${wrapper2Width}px` }}></div>
                </div>
            )}
            <div ref={wrapper2Ref} className={classes.fixedHeaders}>
                <DataTable className={tableClasses} layout="fixed" width="initial">
                    <TableHead className={fixColumns ? classes.tableHeader : ""}>
                        <DataTableRow>
                            {grid.parentColumns.length > 0 && (
                                <CustomDataTableColumnHeader
                                    backgroundColor={section.styles.columns.backgroundColor}
                                    fixed={section.fixedHeaders}
                                ></CustomDataTableColumnHeader>
                            )}
                            {grid.parentColumns.map(column => {
                                return (
                                    <CustomDataTableColumnHeader
                                        backgroundColor={section.styles.columns.backgroundColor}
                                        key={column.name}
                                        className={classes.centerSpan}
                                        colSpan={String(column.colSpan)}
                                        fixed={section.fixedHeaders}
                                    >
                                        <span>{column.name}</span>
                                    </CustomDataTableColumnHeader>
                                );
                            })}
                        </DataTableRow>

                        <DataTableRow>
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                fixed={section.fixedHeaders}
                                top="0"
                            ></CustomDataTableColumnHeader>

                            {grid.columns.map(column => (
                                <CustomDataTableColumnHeader
                                    align="center"
                                    backgroundColor={section.styles.columns.backgroundColor}
                                    fixed={section.fixedHeaders}
                                    top="0"
                                    key={`column-${column.name}`}
                                    colSpan={String(column.totalCategories)}
                                    className={`${classes.columnWidth} ${classes.centerSpan}`}
                                >
                                    {column.name}
                                </CustomDataTableColumnHeader>
                            ))}
                        </DataTableRow>

                        {totalCategories > 0 && (
                            <DataTableRow>
                                <CustomDataTableColumnHeader
                                    backgroundColor={section.styles.columns.backgroundColor}
                                    fixed={section.fixedHeaders}
                                    top="0"
                                ></CustomDataTableColumnHeader>

                                {grid.columns.map(column =>
                                    column.categories?.map((category, idx) => (
                                        <CustomDataTableColumnHeader
                                            backgroundColor={section.styles.columns.backgroundColor}
                                            fixed={section.fixedHeaders}
                                            top="0"
                                            key={`column-${column.name}-${category}-${idx}`}
                                            className={`${classes.columnWidth} ${classes.centerSpan}`}
                                        >
                                            <span>{category}</span>
                                        </CustomDataTableColumnHeader>
                                    ))
                                )}
                            </DataTableRow>
                        )}
                    </TableHead>

                    <TableBody>
                        {grid.rows.map(row => (
                            <DataTableRow key={row.id}>
                                <CustomDataTableCell
                                    backgroundColor={section.styles.rows.backgroundColor}
                                    className={classes.td}
                                    position={fixRows ? "sticky" : undefined}
                                    left={fixRows ? "0px" : undefined}
                                >
                                    <span>{row.name}</span>
                                </CustomDataTableCell>

                                {row.items.map((item, idx) =>
                                    item.dataElement?.cocId && row.cellsVisible ? (
                                        <CustomDataTableCell
                                            backgroundColor={section.styles.rows.backgroundColor}
                                            key={item.dataElement.id + item.dataElement.cocId}
                                        >
                                            <DataElementItem
                                                dataElement={item.dataElement}
                                                dataFormInfo={dataFormInfo}
                                                noComment={section.disableComments}
                                                columnDataElements={
                                                    item.columnDataElements ? item.columnDataElements : undefined
                                                }
                                                columnTotal={item.columnTotal}
                                                manualyDisabled={item.disabled}
                                            />
                                        </CustomDataTableCell>
                                    ) : (
                                        <CustomDataTableCell
                                            backgroundColor={section.styles.rows.backgroundColor}
                                            key={`cell-${idx}`}
                                        ></CustomDataTableCell>
                                    )
                                )}
                            </DataTableRow>
                        ))}
                    </TableBody>
                </DataTable>
            </div>
        </DataTableSection>
    );
};

const useStyles = makeStyles({
    wrapper: { margin: 10 },
    header: { fontSize: "1.4em", fontWeight: "bold" as const },
    table: { borderWidth: "3px !important", minWidth: "100%" },
    td: { minWidth: "400px !important" },
    columnWidth: { minWidth: "6.2em !important" },
    source: { maxWidth: "35% !important", width: "33% !important", minWidth: "15% !important" },
    centerSpan: {
        "& span": {
            alignItems: "center",
        },
    },
    tableHeader: { position: "sticky", top: 0, zIndex: 3 },
    fixedHeaders: fixHeaderClasses.fixedHeaders,
});

export default React.memo(GridWithCategoryColumns);
