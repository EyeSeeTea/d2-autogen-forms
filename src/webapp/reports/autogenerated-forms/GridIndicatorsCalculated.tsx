import React from "react";
import {
    DataTable,
    TableHead,
    DataTableRow,
    TableBody,
    // @ts-ignore
} from "@dhis2/ui";
import { GridIndicatorsCalculatedViewModel } from "./GridIndicatorsCalculatedViewModel";
import { DataFormInfo } from "./AutogeneratedForm";
import { SectionWithIndicatorsCalculated } from "../../../domain/common/entities/DataForm";
import { makeStyles } from "@material-ui/core";
import DataTableSection from "./DataTableSection";
import { CustomDataTableCell, CustomDataTableColumnHeader } from "./datatables/CustomDataTables";
import i18n from "../../../locales";
import { DataElementItem } from "./DataElementItem";
import { CustomInput } from "./widgets/NumberWidget";
import { InputFormula } from "./datatables/InputFormula";

export interface GridIndicatorsCalculatedProps {
    dataFormInfo: DataFormInfo;
    section: SectionWithIndicatorsCalculated;
}

const GridIndicatorsCalculated: React.FC<GridIndicatorsCalculatedProps> = props => {
    const { section, dataFormInfo } = props;
    const grid = React.useMemo(
        () => GridIndicatorsCalculatedViewModel.get(section, dataFormInfo),
        [dataFormInfo, section]
    );

    const classes = useStyles();

    return (
        <DataTableSection section={grid} sectionStyles={props.section.styles} dataFormInfo={dataFormInfo}>
            {grid.tables.map(table => (
                <DataTable key={table.rowName} className={classes.table} layout="fixed">
                    <TableHead>
                        <DataTableRow>
                            <CustomDataTableColumnHeader
                                width="200px"
                                backgroundColor={props.section.styles.columns.backgroundColor}
                            ></CustomDataTableColumnHeader>
                            <CustomDataTableColumnHeader
                                backgroundColor={props.section.styles.columns.backgroundColor}
                                width="50px"
                            >
                                {i18n.t("Period")}
                            </CustomDataTableColumnHeader>
                            {table.columns.map(column => {
                                return (
                                    <CustomDataTableColumnHeader
                                        backgroundColor={props.section.styles.columns.backgroundColor}
                                        key={column}
                                    >
                                        {i18n.t(column)}
                                    </CustomDataTableColumnHeader>
                                );
                            })}
                        </DataTableRow>
                    </TableHead>

                    <TableBody>
                        {grid.periods.map((period, periodRowIndex) => (
                            <DataTableRow key={`${period}_${table.rowName}`}>
                                {periodRowIndex === 0 && (
                                    <CustomDataTableCell
                                        rowSpan={grid.periods.length.toString()}
                                        backgroundColor={props.section.styles.rows.backgroundColor}
                                    >
                                        <span>{table.rowName}</span>
                                    </CustomDataTableCell>
                                )}
                                <CustomDataTableCell backgroundColor={props.section.styles.rows.backgroundColor}>
                                    <span>{period}</span>
                                </CustomDataTableCell>
                                {table.rows.map(row => {
                                    return row.cells.map(cell => {
                                        return (
                                            <CustomDataTableCell
                                                key={`${cell.columnName}_${period}`}
                                                backgroundColor={props.section.styles.rows.backgroundColor}
                                            >
                                                {cell.formula ? (
                                                    <InputFormula
                                                        dataFormInfo={dataFormInfo}
                                                        formula={cell.formula.formula}
                                                        period={period}
                                                        dataElementCodes={cell.formula.dataElementCodes}
                                                    />
                                                ) : cell.dataElement ? (
                                                    <DataElementItem
                                                        manualyDisabled={cell.disabled}
                                                        dataElement={cell.dataElement}
                                                        dataFormInfo={dataFormInfo}
                                                        period={period}
                                                    />
                                                ) : (
                                                    <CustomInput
                                                        disabled={cell.disabled}
                                                        defaultValue={cell.defaultValue}
                                                    />
                                                )}
                                            </CustomDataTableCell>
                                        );
                                    });
                                })}
                            </DataTableRow>
                        ))}
                    </TableBody>
                </DataTable>
            ))}
        </DataTableSection>
    );
};

const useStyles = makeStyles({
    wrapper: { margin: 10 },
    header: { fontSize: "1.4em", fontWeight: "bold" as const },
    table: { borderWidth: "3px !important" },
    center: { textAlign: "center" },
    rowTitle: { fontSize: "1.2em", fontWeight: "bold" as const },
});

export default React.memo(GridIndicatorsCalculated);
