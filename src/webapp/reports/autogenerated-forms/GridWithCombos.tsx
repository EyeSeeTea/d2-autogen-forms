import React from "react";
import {
    DataTable,
    TableHead,
    DataTableRow,
    TableBody,
    // @ts-ignore
} from "@dhis2/ui";
import { GridWithCombosViewModel } from "./GridWithCombosViewModel";
import { DataFormInfo } from "./AutogeneratedForm";
import { Section } from "../../../domain/common/entities/DataForm";
import { DataElementItem } from "./DataElementItem";
import { makeStyles } from "@material-ui/core";
import DataTableSection from "./DataTableSection";
import { CustomDataTableCell, CustomDataTableColumnHeader, fixHeaderClasses } from "./datatables/CustomDataTables";

export interface GridWithCombosProps {
    dataFormInfo: DataFormInfo;
    section: Section;
}

const GridWithCombos: React.FC<GridWithCombosProps> = props => {
    const { dataFormInfo } = props;
    const grid = React.useMemo(() => GridWithCombosViewModel.get(props.section), [props.section]);
    const classes = useStyles();

    return (
        <DataTableSection section={grid} sectionStyles={props.section.styles} dataFormInfo={dataFormInfo}>
            <div className={classes.fixedHeaders}>
                <DataTable className={classes.table} layout="fixed" width="initial">
                    <TableHead className={classes.tableHeader}>
                        <DataTableRow>
                            {grid.parentColumns.length > 0 && (
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                ></CustomDataTableColumnHeader>
                            )}
                            {grid.parentColumns.map(column => {
                                return (
                                    <CustomDataTableColumnHeader
                                        backgroundColor={props.section.styles.columns.backgroundColor}
                                        key={column.name}
                                        className={classes.centerSpan}
                                        colSpan={String(column.colSpan)}
                                    >
                                        <span>{column.name}</span>
                                    </CustomDataTableColumnHeader>
                                );
                            })}
                        </DataTableRow>

                        <DataTableRow>
                            {grid.useIndexes ? (
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                    width="30px"
                                >
                                    <span className={classes.header}>#</span>{" "}
                                </CustomDataTableColumnHeader>
                            ) : (
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                    fixed
                                    top="0"
                                ></CustomDataTableColumnHeader>
                            )}

                            {grid.columns.map(column => (
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                    fixed
                                    top="0"
                                    key={`column-${column.name}`}
                                    className={
                                        column.name === "Source type for HWF - (Inputs & Outputs)"
                                            ? classes.source
                                            : classes.columnWidth
                                    }
                                >
                                    <span>{column.cocName}</span>
                                </CustomDataTableColumnHeader>
                            ))}
                        </DataTableRow>
                    </TableHead>

                    <TableBody>
                        {grid.rows.map((row, idx) => (
                            <DataTableRow key={`policy-${row.name}`}>
                                <CustomDataTableCell
                                    backgroundColor={props.section.styles.rows.backgroundColor}
                                    className={classes.td}
                                >
                                    <span>{grid.useIndexes ? (idx + 1).toString() : row.name}</span>
                                </CustomDataTableCell>

                                {row.items.map((item, idx) =>
                                    item.dataElement ? (
                                        <CustomDataTableCell
                                            backgroundColor={props.section.styles.rows.backgroundColor}
                                            key={item.dataElement.id + item.dataElement.cocId}
                                        >
                                            <DataElementItem
                                                dataElement={item.dataElement}
                                                dataFormInfo={dataFormInfo}
                                                noComment={
                                                    item.column.name !== "Source type for HWF - (Inputs & Outputs)"
                                                }
                                            />
                                        </CustomDataTableCell>
                                    ) : (
                                        <CustomDataTableCell
                                            backgroundColor={props.section.styles.rows.backgroundColor}
                                            key={`cell-${idx}`}
                                        ></CustomDataTableCell>
                                    )
                                )}
                            </DataTableRow>
                        ))}
                    </TableBody>
                </DataTable>
            </div>
        </DataTableSection>
    );
};

const useStyles = makeStyles({
    wrapper: { margin: 10 },
    header: { fontSize: "1.4em", fontWeight: "bold" as const },
    table: { borderWidth: "3px !important", minWidth: "100%" },
    td: { minWidth: "400px !important" },
    columnWidth: { minWidth: "6.2em !important" },
    source: { maxWidth: "35% !important", width: "33% !important", minWidth: "15% !important" },
    centerSpan: {
        "& span": {
            alignItems: "center",
        },
    },
    tableHeader: { position: "sticky", insetBlockStart: 0, zIndex: 2 },
    fixedHeaders: fixHeaderClasses.fixedHeaders,
});

export default React.memo(GridWithCombos);
