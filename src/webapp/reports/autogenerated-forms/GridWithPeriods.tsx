import React from "react";
import {
    DataTable,
    TableHead,
    DataTableRow,
    TableBody,
    // @ts-ignore
} from "@dhis2/ui";
import { GridWithPeriodsI, DataElementSubGroupRow, GridWithPeriodsViewModel } from "./GridWithPeriodsViewModel";
import { DataFormInfo } from "./AutogeneratedForm";
import { SectionWithPeriods } from "../../../domain/common/entities/DataForm";
import { DataElementItem } from "./DataElementItem";
import { makeStyles, Tabs, Tab } from "@material-ui/core";
import { DataElement } from "../../../domain/common/entities/DataElement";
import DataTableSection from "./DataTableSection";
import { Html } from "./Html";
import { DataTableCellRowName } from "./datatables/DataTableCellRowName";
import { CustomDataTableCell, CustomDataTableColumnHeader } from "./datatables/CustomDataTables";
import { DataTableCellFormula } from "./datatables/DataTableCellFormula";
import { RowIndicatorItem } from "../../components/IndicatorItem/IndicatorItem";
import { checkIndicatorDirection } from "../../../domain/common/entities/Indicator";

/*
 * Convert data forms into table, using "-" as a separator. An example for section ITNs:
 *
 *    - ITNs - Basic
 *    - ITNs - Extended - Written Policy
 *    - ITNs - Extended - Policy Implemented
 *    - ITNs - Extended - Policy Extra
 *
 *    This will create this table:
 *
 *
 *    ITNs                                   |  2020  |  2021  |  2022   |
 *    --------------------------------------------------------------------
 *    ITNs - Basic                           |        |        |         |
 *                     | Written Policy      |        |        |         |
 *    ITNs - Extended  | Policy Implemented  |        |        |         |
 *                     | Policy Extra        |        |        |         |
 **/

export interface GridWithPeriodsProps {
    dataFormInfo: DataFormInfo;
    section: SectionWithPeriods;
}

const GridWithPeriods: React.FC<GridWithPeriodsProps> = props => {
    const [activeTab, setActiveTab] = React.useState(0);
    const { section, dataFormInfo } = props;
    const grid = React.useMemo(() => GridWithPeriodsViewModel.get(section, dataFormInfo), [dataFormInfo, section]);

    const handleChange = (_: React.ChangeEvent<{}>, value: number) => {
        setActiveTab(value);
    };

    const categoryOptionCombination = grid.periodTabs[activeTab]?.id;

    return (
        <DataTableSection section={grid} sectionStyles={props.section.styles} dataFormInfo={dataFormInfo}>
            {grid.periodTabs.length > 0 ? (
                <>
                    <Tabs value={activeTab} onChange={handleChange}>
                        {grid.periodTabs.map(tabPeriod => {
                            return <Tab key={tabPeriod.id + "Tab"} label={tabPeriod.name} />;
                        })}
                    </Tabs>
                    {grid.periodTabs.map(tabPeriod => {
                        if (tabPeriod.id !== categoryOptionCombination) return null;
                        return (
                            <PeriodTable
                                categoryOptionComboId={tabPeriod.id}
                                key={`${tabPeriod.id}_table`}
                                grid={grid}
                                dataFormInfo={dataFormInfo}
                                section={section}
                            />
                        );
                    })}
                </>
            ) : (
                <PeriodTable section={section} grid={grid} dataFormInfo={dataFormInfo} />
            )}
        </DataTableSection>
    );
};

type PeriodTableProps = {
    dataFormInfo: DataFormInfo;
    categoryOptionComboId?: string;
    grid: GridWithPeriodsI;
    section: SectionWithPeriods;
};

const PeriodTable: React.FC<PeriodTableProps> = props => {
    const { grid, dataFormInfo, categoryOptionComboId, section } = props;
    const classes = useStyles();

    const hasRowsWithSubGroups = grid.rows.some(row => row.type === "subGroup");
    const periodIds = grid.periods.map(period => period.id);

    return (
        <DataTable className={classes.table}>
            <TableHead>
                <DataTableRow>
                    <CustomDataTableColumnHeader
                        width="400px"
                        colSpan={hasRowsWithSubGroups ? "3" : "2"}
                        backgroundColor={props.section.styles.columns.backgroundColor}
                    ></CustomDataTableColumnHeader>
                    {grid.periods.map(period => (
                        <CustomDataTableColumnHeader
                            backgroundColor={props.section.styles.columns.backgroundColor}
                            key={period.id}
                        >
                            <span>{period.label}</span>
                        </CustomDataTableColumnHeader>
                    ))}
                </DataTableRow>
            </TableHead>

            <TableBody>
                {grid.rows.map(row => {
                    switch (row.type) {
                        case "dataElement":
                            return (
                                <React.Fragment key={row.dataElement.id}>
                                    {row.indicator && checkIndicatorDirection(row.indicator, "before") && (
                                        <RowIndicatorItem
                                            indicator={row.indicator}
                                            colSpan={hasRowsWithSubGroups ? "3" : "2"}
                                            dataFormInfo={dataFormInfo}
                                            periods={periodIds}
                                        />
                                    )}
                                    <DataTableRow>
                                        <CustomDataTableCell
                                            backgroundColor={props.section.styles.rows.backgroundColor}
                                            colSpan={hasRowsWithSubGroups ? "3" : "2"}
                                        >
                                            <DataTableCellRowName
                                                html={row.dataElement.htmlText}
                                                name={row.dataElement.name}
                                            />
                                        </CustomDataTableCell>

                                        <DataTableDataElementCell
                                            grid={grid}
                                            dataElement={row.dataElement}
                                            dataFormInfo={dataFormInfo}
                                            categoryOptionComboId={categoryOptionComboId}
                                            section={section}
                                        />
                                    </DataTableRow>
                                    {row.indicator && checkIndicatorDirection(row.indicator, "after") && (
                                        <RowIndicatorItem
                                            indicator={row.indicator}
                                            colSpan={hasRowsWithSubGroups ? "3" : "2"}
                                            dataFormInfo={dataFormInfo}
                                            periods={periodIds}
                                        />
                                    )}
                                </React.Fragment>
                            );
                        case "dataElementFile":
                            return (
                                <DataTableRow key={row.dataElement.id}>
                                    <CustomDataTableCell
                                        backgroundColor={props.section.styles.rows.backgroundColor}
                                        colSpan="2"
                                    >
                                        <span>{row.dataElement.name}</span>
                                        <Html content={row.dataElement.description}></Html>
                                    </CustomDataTableCell>

                                    <CustomDataTableCell
                                        backgroundColor={props.section.styles.rows.backgroundColor}
                                        colSpan={grid.periods.length.toString()}
                                    >
                                        <CustomDataTableCell>
                                            <DataElementItem
                                                noComment={row.dataElement.disabledComments}
                                                dataElement={{ ...row.dataElement, cocId: categoryOptionComboId }}
                                                dataFormInfo={dataFormInfo}
                                                lockException={grid.lockException}
                                            />
                                        </CustomDataTableCell>
                                    </CustomDataTableCell>
                                </DataTableRow>
                            );

                        case "group":
                            return row.rows.map((row2, idx) => (
                                <React.Fragment key={`${idx}-${row.name}`}>
                                    {row2.indicator && checkIndicatorDirection(row2.indicator, "before") && (
                                        <RowIndicatorItem
                                            indicator={row2.indicator}
                                            colSpan=""
                                            dataFormInfo={dataFormInfo}
                                            periods={periodIds}
                                        />
                                    )}
                                    <DataTableRow>
                                        {idx === 0 && (
                                            <CustomDataTableCell
                                                backgroundColor={props.section.styles.rows.backgroundColor}
                                                rowSpan={String(
                                                    row.rows.length + row.rows.filter(x => Boolean(x.indicator)).length
                                                )}
                                            >
                                                <span className={classes.rowTitle}>{row.name}</span>
                                                <Html content={row.groupDescription} />
                                            </CustomDataTableCell>
                                        )}

                                        <CustomDataTableCell
                                            backgroundColor={props.section.styles.rows.backgroundColor}
                                            colSpan={grid.rows.some(row => row.type === "subGroup") ? "2" : "0"}
                                        >
                                            <DataTableCellRowName
                                                html={row2.dataElement.htmlText}
                                                name={row2.dataElement.name}
                                            />
                                        </CustomDataTableCell>

                                        <DataTableDataElementCell
                                            grid={grid}
                                            dataElement={row2.dataElement}
                                            dataFormInfo={dataFormInfo}
                                            categoryOptionComboId={categoryOptionComboId}
                                            section={section}
                                        />
                                    </DataTableRow>
                                    {row2.indicator && checkIndicatorDirection(row2.indicator, "after") && (
                                        <RowIndicatorItem
                                            indicator={row2.indicator}
                                            colSpan=""
                                            dataFormInfo={dataFormInfo}
                                            periods={periodIds}
                                        />
                                    )}
                                </React.Fragment>
                            ));
                        case "subGroup":
                            return row.rows.map((subGroupRow: DataElementSubGroupRow) => {
                                return (
                                    <React.Fragment key={subGroupRow.dataElement.id}>
                                        {subGroupRow.indicator &&
                                            checkIndicatorDirection(subGroupRow.indicator, "before") && (
                                                <RowIndicatorItem
                                                    indicator={subGroupRow.indicator}
                                                    colSpan={subGroupRow.colSpan}
                                                    dataFormInfo={dataFormInfo}
                                                    periods={periodIds}
                                                />
                                            )}
                                        <DataTableRow>
                                            {subGroupRow.groupName && (
                                                <CustomDataTableCell
                                                    backgroundColor={props.section.styles.rows.backgroundColor}
                                                    rowSpan={row.rows.length.toString()}
                                                    className={classes.rowTitle}
                                                >
                                                    <span>{subGroupRow.groupName}</span>
                                                </CustomDataTableCell>
                                            )}

                                            {subGroupRow.subGroup && (
                                                <CustomDataTableCell
                                                    backgroundColor={props.section.styles.rows.backgroundColor}
                                                    rowSpan={subGroupRow.dataElementsPerSubGroup.toString()}
                                                    className={classes.rowTitle}
                                                >
                                                    <span>{subGroupRow.subGroup}</span>
                                                </CustomDataTableCell>
                                            )}

                                            <CustomDataTableCell
                                                backgroundColor={props.section.styles.rows.backgroundColor}
                                                colSpan={subGroupRow.colSpan}
                                            >
                                                <span>{subGroupRow.dataElement.name}</span>
                                            </CustomDataTableCell>

                                            <DataTableDataElementCell
                                                grid={grid}
                                                dataElement={subGroupRow.dataElement}
                                                dataFormInfo={dataFormInfo}
                                                section={section}
                                            />
                                        </DataTableRow>
                                        {subGroupRow.indicator &&
                                            checkIndicatorDirection(subGroupRow.indicator, "after") && (
                                                <RowIndicatorItem
                                                    indicator={subGroupRow.indicator}
                                                    colSpan={subGroupRow.colSpan}
                                                    dataFormInfo={dataFormInfo}
                                                    periods={periodIds}
                                                />
                                            )}
                                    </React.Fragment>
                                );
                            });
                    }
                })}
                {grid.summary.map(summary => (
                    <DataTableRow key="total-custom-row">
                        <CustomDataTableCell
                            backgroundColor={section.styles.totals.backgroundColor}
                            key="total-column-name-periods"
                            colSpan={hasRowsWithSubGroups ? "3" : "2"}
                        >
                            <Html content={summary.cellName} />
                        </CustomDataTableCell>
                        {summary.cells.map(itemTotal => {
                            return (
                                <DataTableCellFormula
                                    key={itemTotal.columnName}
                                    dataFormInfo={dataFormInfo}
                                    styles={section.styles}
                                    total={itemTotal}
                                    formula={itemTotal.formula}
                                    period={itemTotal.columnName}
                                />
                            );
                        })}
                    </DataTableRow>
                ))}

                {grid.indicators.map(indicator => {
                    return (
                        <RowIndicatorItem
                            key={`parent_${indicator.id}`}
                            indicator={indicator}
                            colSpan={hasRowsWithSubGroups ? "3" : "2"}
                            dataFormInfo={dataFormInfo}
                            periods={periodIds}
                        />
                    );
                })}
            </TableBody>
        </DataTable>
    );
};

interface DataTableDataElementCellProps {
    dataElement: DataElement;
    dataFormInfo: DataFormInfo;
    categoryOptionComboId?: string;
    section: SectionWithPeriods;
    grid: GridWithPeriodsI;
}

const DataTableDataElementCell: React.FC<DataTableDataElementCellProps> = props => {
    const {
        dataElement,
        dataFormInfo,
        categoryOptionComboId,
        section,
        grid: { lockException, periods },
    } = props;
    const cocId = categoryOptionComboId || dataElement.cocId;

    return (
        <>
            {periods.map(period => (
                <CustomDataTableCell
                    backgroundColor={section.styles.rows.backgroundColor}
                    key={[dataElement.id, period].join("-")}
                >
                    <DataElementItem
                        noComment={dataElement.disabledComments}
                        dataElement={{ ...dataElement, cocId: cocId }}
                        dataFormInfo={dataFormInfo}
                        period={period.id}
                        lockException={lockException}
                    />
                </CustomDataTableCell>
            ))}
        </>
    );
};

const useStyles = makeStyles({
    wrapper: { margin: 10 },
    header: { fontSize: "1.4em", fontWeight: "bold" as const },
    table: { borderWidth: "3px !important" },
    center: { textAlign: "center" },
    rowTitle: { fontSize: "1.2em", fontWeight: "bold" as const },
});

export default React.memo(GridWithPeriods);
