import { DataElement } from "../../../../domain/common/entities/DataElement";
import { DataFormInfo } from "../AutogeneratedForm";

type UseOUToggleState = {
    isOUToggleVisible: boolean;
    isOUToggleDisabled: boolean;
};

export function useOUToggle(dataFormInfo: DataFormInfo, dataElement: DataElement): UseOUToggleState {
    const section = dataFormInfo.metadata.dataForm.sections.find(section =>
        section.dataElements.some(de => de.id === dataElement.id)
    );
    if (!section) return { isOUToggleDisabled: false, isOUToggleVisible: true };

    const { toggle } = section;
    if (toggle.type !== "orgUnit") return { isOUToggleDisabled: false, isOUToggleVisible: true };
    if (toggle.dataElements.length === 0 && toggle.disabled)
        return { isOUToggleDisabled: true, isOUToggleVisible: true };

    const orgUnitCode = dataFormInfo.metadata.dataForm.orgUnit.code;
    const isOrgUnitInToggleList = toggle.orgUnits.includes(orgUnitCode);
    const dataElementExistsInToggle = toggle.dataElements.includes(dataElement.code);

    switch (toggle.condition) {
        case "hide": {
            if (isOrgUnitInToggleList && dataElementExistsInToggle) {
                if (toggle.disabled) return { isOUToggleDisabled: true, isOUToggleVisible: true };
                return { isOUToggleDisabled: false, isOUToggleVisible: false };
            }
            return { isOUToggleDisabled: false, isOUToggleVisible: true };
        }
        case "show": {
            if (isOrgUnitInToggleList && dataElementExistsInToggle)
                return { isOUToggleDisabled: false, isOUToggleVisible: true };
            else {
                if (toggle.disabled) return { isOUToggleDisabled: true, isOUToggleVisible: true };
                return { isOUToggleDisabled: false, isOUToggleVisible: false };
            }
        }
    }
}
