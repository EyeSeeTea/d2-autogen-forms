import { useMemo } from "react";
import { Section } from "../../../../domain/common/entities/DataForm";
import { ToggleDataElement } from "../../../../domain/common/entities/ToggleMultiple";
import { DataFormInfo } from "../AutogeneratedForm";
import { evaluateTotalRule, verifyConditionByDataValueType } from "./useApplyRules";
import { isDataValueEnabled } from "../DataElementItem";
import { DataTableSectionObj } from "../DataTableSection";

export type SectionVisibilityState = {
    isDisabled: boolean;
    isOpen: boolean;
    isVisible: boolean;
};

export function useSectionVisibility(section: DataTableSectionObj, dataFormInfo: DataFormInfo): SectionVisibilityState {
    const { toggle, toggleMultiple } = section;
    const sectionTotalRules = useMemo(
        () =>
            dataFormInfo.metadata.dataForm.totalRules.sectionTotalRules.filter(sectionTotalRule =>
                sectionTotalRule.sections.includes(section.id)
            ),
        [dataFormInfo, section.id]
    );

    const isDisabled = useMemo(() => {
        if (toggle.type === "none") return false;
        return toggle.disabled;
    }, [toggle]);

    const isOpen = useMemo(() => {
        return evaluateSectionOpenState(toggle, dataFormInfo);
    }, [toggle, dataFormInfo]);

    const isVisibleFromTotals = useMemo(() => {
        if (sectionTotalRules.length === 0) return true;
        return sectionTotalRules.map(rule => evaluateTotalRule(rule, dataFormInfo)).some(value => value);
    }, [dataFormInfo, sectionTotalRules]);

    const isVisible = useMemo(() => {
        if (toggle.type === "dataElementExternal" && !isOpen) {
            return false;
        }

        if (toggle.type === "orgUnit" && toggle.dataElements.length === 0) {
            const visibility = evaluateOrgUnitToggle(dataFormInfo, toggle);
            if (visibility !== null) return visibility;
        }

        if (!toggleMultiple) return true;

        if (sectionTotalRules.length !== 0 && !isVisibleFromTotals) {
            return false;
        }

        if (toggleMultiple.toggleDataElements.length !== 0) {
            return evaluateToggleMultiple(dataFormInfo, toggleMultiple);
        }

        return true;
    }, [toggle, toggleMultiple, sectionTotalRules.length, isVisibleFromTotals, isOpen, dataFormInfo]);

    return { isDisabled: isDisabled, isOpen: isOpen, isVisible: isVisible };
}

function evaluateSectionOpenState(toggle: Section["toggle"], dataFormInfo: DataFormInfo): boolean {
    switch (toggle.type) {
        case "dataElement": {
            const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
            return isDataValueEnabled(dataValue);
        }
        case "dataElementExternal": {
            const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
            return verifyConditionByDataValueType(dataValue, toggle);
        }
        default:
            return true;
    }
}

function evaluateOrgUnitToggle(
    dataFormInfo: DataFormInfo,
    toggle: Extract<Section["toggle"], { type: "orgUnit" }>
): boolean | null {
    const orgUnitCode = dataFormInfo.metadata.dataForm.orgUnit.code;
    const isOrgUnitInList = toggle.orgUnits.includes(orgUnitCode);

    if (!orgUnitCode) {
        console.warn(
            `Organization unit ${dataFormInfo.metadata.dataForm.orgUnit.id} has no code. ` +
                `Cannot evaluate orgUnit toggle for section. Defaulting to visible.`
        );
        return null;
    }

    switch (toggle.condition) {
        case "hide":
            return !isOrgUnitInList;
        case "show":
            return isOrgUnitInList;
        default:
            console.error(`Unknown orgUnit toggle condition: ${toggle.condition}`);
            return null;
    }
}

function evaluateToggleMultiple(
    dataFormInfo: DataFormInfo,
    toggleMultiple: NonNullable<Section["toggleMultiple"]>
): boolean {
    const { logicalOperator, toggleDataElements } = toggleMultiple;

    const evaluateToggleCondition = (toggle: ToggleDataElement): boolean => {
        const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
        return verifyConditionByDataValueType(dataValue, toggle);
    };

    if (logicalOperator === "OR") {
        return toggleDataElements.some(evaluateToggleCondition);
    }
    return toggleDataElements.every(evaluateToggleCondition);
}
