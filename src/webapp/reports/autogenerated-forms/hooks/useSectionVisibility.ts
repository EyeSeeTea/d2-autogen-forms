import { useMemo } from "react";
import { Section } from "../../../../domain/common/entities/DataForm";
import { DataElementToggle } from "../../../../domain/common/entities/ToggleMultiple";
import { DataFormInfo } from "../AutogeneratedForm";
import { evaluateTotalRule, verifyConditionByDataValueType } from "./useApplyRules";
import { isDataValueEnabled } from "../DataElementItem";
import { DataTableSectionObj } from "../DataTableSection";
import { Maybe } from "../../../../utils/ts-utils";

export type SectionVisibilityState = {
    isDisabled: boolean;
    isOpen: boolean;
    isVisible: boolean;
};

export function useSectionVisibility(section: DataTableSectionObj, dataFormInfo: DataFormInfo): SectionVisibilityState {
    return useMemo(() => getSectionVisibilityState(section, dataFormInfo), [section, dataFormInfo]);
}

type SectionVisibilityInput = {
    id: DataTableSectionObj["id"];
    hidden?: DataTableSectionObj["hidden"];
    toggle: DataTableSectionObj["toggle"];
    toggleMultiple?: DataTableSectionObj["toggleMultiple"];
};

export function getSectionVisibilityState(
    section: SectionVisibilityInput,
    dataFormInfo: DataFormInfo
): SectionVisibilityState {
    const { toggle, toggleMultiple } = section;
    const sectionTotalRules = dataFormInfo.metadata.dataForm.totalRules.sectionTotalRules.filter(sectionTotalRule =>
        sectionTotalRule.sections.includes(section.id)
    );

    const isOpen = evaluateSectionOpenState(toggle, dataFormInfo);

    const isVisibleFromTotals =
        sectionTotalRules.length === 0
            ? true
            : sectionTotalRules.map(rule => evaluateTotalRule(rule, dataFormInfo)).some(value => value);
    const toggleMultipleState = toggleMultiple ? evaluateToggleMultipleState(dataFormInfo, toggleMultiple) : undefined;

    const isDisabled = (() => {
        if (toggle.type === "none" && !toggleMultiple) return false;
        if (toggleMultipleState) return toggleMultipleState.isDisabled;
        return toggle.type !== "none" ? toggle.disabled : false;
    })();

    const isVisible = (() => {
        if (section.hidden) return !section.hidden;
        if (toggle.type === "dataElementExternal" && !isOpen) return false;
        if (sectionTotalRules.length > 0 && !isVisibleFromTotals) return false;
        if (toggleMultipleState) return toggleMultipleState.isVisible;
        return true;
    })();

    return { isDisabled, isOpen, isVisible };
}

function evaluateSectionOpenState(toggle: Section["toggle"], dataFormInfo: DataFormInfo): boolean {
    switch (toggle.type) {
        case "dataElement": {
            const dataValues = toggle.dataElement.categoryOptionCombos.map(coc => {
                const updatedDataElement = {
                    ...toggle.dataElement,
                    cocId: coc.id,
                };
                return dataFormInfo.data.values.getOrEmpty(updatedDataElement, dataFormInfo);
            });
            return dataValues.some(dv => isDataValueEnabled(dv));
        }
        case "dataElementExternal": {
            const dataValues = toggle.dataElement.categoryOptionCombos.map(coc => {
                const updatedDataElement = {
                    ...toggle.dataElement,
                    cocId: coc.id,
                };
                return dataFormInfo.data.values.getOrEmpty(updatedDataElement, dataFormInfo);
            });
            return dataValues.some(dv => verifyConditionByDataValueType(dv, toggle));
        }
        default:
            return true;
    }
}

type ToggleMultipleConditionState = {
    isVisible: boolean;
    isDisabled: boolean;
};

type ConditionsByType = {
    dataElement: ToggleMultipleConditionState[];
    orgUnit: ToggleMultipleConditionState[];
};

export function evaluateToggleMultipleState(
    dataFormInfo: DataFormInfo,
    toggleMultiple: DataElementToggle
): ToggleMultipleConditionState {
    const conditionsByType = getConditionsByType(dataFormInfo, toggleMultiple);
    const hasConditions = conditionsByType.dataElement.length > 0 || conditionsByType.orgUnit.length > 0;
    if (!hasConditions) return { isVisible: true, isDisabled: false };

    const { logicalOperator } = toggleMultiple;

    return {
        isVisible: isSectionVisible(conditionsByType, logicalOperator),
        isDisabled: isSectionDisabled(conditionsByType, logicalOperator),
    };
}

function getConditionsByType(dataFormInfo: DataFormInfo, toggleMultiple: DataElementToggle): ConditionsByType {
    const currentOrgUnitCode = dataFormInfo.orgUnit.code;
    const { toggleDataElements, orgUnitConditions } = toggleMultiple;

    const getToggleConditions = (toggleType: "dataElement" | "orgUnit") =>
        toggleDataElements
            .filter(toggle => toggle.type === toggleType)
            .map(toggle => ({
                isVisible: evaluateToggleCondition(toggle, dataFormInfo, currentOrgUnitCode),
                isDisabled: toggle.dataElement.disabled,
            }));

    const orgUnitConditionResults = orgUnitConditions.map(condition => ({
        isVisible: evaluateOrgUnitCondition(condition.orgUnitCodes, condition.condition, currentOrgUnitCode),
        isDisabled: condition.disabled,
    }));

    return {
        dataElement: getToggleConditions("dataElement"),
        orgUnit: [...getToggleConditions("orgUnit"), ...orgUnitConditionResults],
    };
}

function isSectionVisible(
    conditionsByType: ConditionsByType,
    logicalOperator: DataElementToggle["logicalOperator"]
): boolean {
    const dataElementVisibility = evaluateConditions(conditionsByType.dataElement, logicalOperator, c => c.isVisible);
    const orgUnitVisibility = evaluateConditions(conditionsByType.orgUnit, logicalOperator, c => c.isVisible);
    return dataElementVisibility ?? orgUnitVisibility ?? true;
}

function isSectionDisabled(
    conditionsByType: ConditionsByType,
    logicalOperator: DataElementToggle["logicalOperator"]
): boolean {
    const { dataElement: dataElementConditions, orgUnit: orgUnitConditions } = conditionsByType;

    const dataElementDisabled = evaluateConditions(
        dataElementConditions.filter(c => c.isVisible),
        logicalOperator,
        c => c.isDisabled
    );
    const orgUnitDisabled = evaluateConditions(
        orgUnitConditions.filter(c => c.isVisible),
        logicalOperator,
        c => c.isDisabled
    );

    return dataElementDisabled ?? orgUnitDisabled ?? false;
}

function evaluateConditions(
    conditions: ToggleMultipleConditionState[],
    logicalOperator: DataElementToggle["logicalOperator"],
    evalFunction: (toggleCondition: ToggleMultipleConditionState) => boolean
): Maybe<boolean> {
    if (conditions.length === 0) return undefined;

    return logicalOperator === "AND"
        ? conditions.every(condition => evalFunction(condition))
        : conditions.some(condition => evalFunction(condition));
}

function evaluateToggleCondition(
    toggle: DataElementToggle["toggleDataElements"][number],
    dataFormInfo: DataFormInfo,
    currentOrgUnitCode: string
): boolean {
    switch (toggle.type) {
        case "orgUnit": {
            return evaluateOrgUnitCondition(toggle.orgUnitCodes, toggle.condition, currentOrgUnitCode);
        }
        case "dataElement": {
            const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
            return verifyConditionByDataValueType(dataValue, toggle);
        }
    }
}

function evaluateOrgUnitCondition(orgUnitCodes: string[], condition: string, currentOrgUnitCode: string): boolean {
    const isOrgUnitInToggleList = orgUnitCodes.includes(currentOrgUnitCode);
    return condition === "show" ? isOrgUnitInToggleList : !isOrgUnitInToggleList;
}
