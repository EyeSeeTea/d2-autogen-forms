import React from "react";

import { SectionGrid } from "../../../domain/common/entities/DataForm";
import { DataFormInfo } from "./AutogeneratedForm";
import { DataElementItem } from "./DataElementItem";
import { GridViewModel } from "./GridFormViewModel";

import {
    DataTable,
    TableHead,
    DataTableRow,
    TableBody, // @ts-ignore
} from "@dhis2/ui";
import { makeStyles } from "@material-ui/core";
import DataTableSection from "./DataTableSection";
import { CustomDataTableCell, CustomDataTableColumnHeader } from "./datatables/CustomDataTables";
import { DataTableCellRowName } from "./datatables/DataTableCellRowName";
import _ from "lodash";
import { checkIndicatorDirection } from "../../../domain/common/entities/Indicator";
import { RowIndicatorItem } from "../../components/IndicatorItem/IndicatorItem";
import { DataTableCellFormula } from "./datatables/DataTableCellFormula";
import i18n from "../../../locales";

export interface TableFormProps {
    dataFormInfo: DataFormInfo;
    section: SectionGrid;
}

const TableForm: React.FC<TableFormProps> = React.memo(props => {
    const { dataFormInfo } = props;
    const section = React.useMemo(
        () => GridViewModel.get(props.section, dataFormInfo, "table"),
        [props.section, dataFormInfo]
    );
    const classes = useStyles();

    return (
        <DataTableSection section={section} sectionStyles={props.section.styles} dataFormInfo={dataFormInfo}>
            <DataTable>
                <TableHead>
                    <DataTableRow>
                        {!_.isEmpty(section.columns) && (
                            <CustomDataTableColumnHeader
                                backgroundColor={props.section.styles.columns.backgroundColor}
                                colSpan={section.dataEntryPeriod ? "1" : "2"}
                            >
                                <span className={classes.header}>{section.name}</span>
                            </CustomDataTableColumnHeader>
                        )}

                        {section.dataEntryPeriod && (
                            <>
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                    width="50px"
                                >
                                    {i18n.t("Period")}
                                </CustomDataTableColumnHeader>
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                    colSpan={"1"}
                                ></CustomDataTableColumnHeader>
                            </>
                        )}
                    </DataTableRow>
                </TableHead>

                <TableBody>
                    {section.indicatorsPosition === "start" &&
                        section.indicators.map(indicator => {
                            return (
                                <RowIndicatorItem
                                    key={`parent_${indicator.id}`}
                                    indicator={indicator}
                                    colSpan="0"
                                    dataFormInfo={dataFormInfo}
                                    periods={[section.dataEntryPeriod?.id || dataFormInfo.period]}
                                />
                            );
                        })}

                    {section.dataElements.map(dataElement => {
                        return (
                            <React.Fragment key={dataElement.id}>
                                {dataElement.indicator && checkIndicatorDirection(dataElement.indicator, "before") && (
                                    <RowIndicatorItem
                                        indicator={dataElement.indicator}
                                        colSpan={section.dataEntryPeriod ? "2" : "1"}
                                        dataFormInfo={dataFormInfo}
                                        periods={[section.dataEntryPeriod?.id || dataFormInfo.period]}
                                    />
                                )}

                                <DataTableRow>
                                    <CustomDataTableCell backgroundColor={props.section.styles.rows.backgroundColor}>
                                        <DataTableCellRowName html={dataElement.htmlText} name={dataElement.name} />
                                    </CustomDataTableCell>

                                    {section.dataEntryPeriod && (
                                        <CustomDataTableCell
                                            backgroundColor={props.section.styles.rows.backgroundColor}
                                        >
                                            <span>{section.dataEntryPeriod.label}</span>
                                        </CustomDataTableCell>
                                    )}

                                    <CustomDataTableCell
                                        backgroundColor={props.section.styles.rows.backgroundColor}
                                        key={dataElement.id}
                                    >
                                        <DataElementItem
                                            dataElement={dataElement}
                                            dataFormInfo={dataFormInfo}
                                            noComment={dataElement.disabledComments}
                                            period={section.dataEntryPeriod?.id}
                                            lockException={section.dataEntryPeriod !== undefined}
                                            manualyDisabled={dataElement.disabled}
                                        />
                                    </CustomDataTableCell>
                                </DataTableRow>

                                {dataElement.indicator && checkIndicatorDirection(dataElement.indicator, "after") && (
                                    <RowIndicatorItem
                                        indicator={dataElement.indicator}
                                        colSpan={section.dataEntryPeriod ? "2" : "1"}
                                        dataFormInfo={dataFormInfo}
                                        periods={[section.dataEntryPeriod?.id || dataFormInfo.period]}
                                    />
                                )}
                            </React.Fragment>
                        );
                    })}

                    {section.summary.map(summary => (
                        <DataTableRow key={`total-custom-row-${summary.cellName}`}>
                            <CustomDataTableCell
                                backgroundColor={props.section.styles.totals.backgroundColor}
                                key={`total-column-${summary.cellName}`}
                            >
                                {summary.cellName}
                            </CustomDataTableCell>
                            {summary.cells.map(itemTotal => {
                                return (
                                    <DataTableCellFormula
                                        key={itemTotal.columnName}
                                        dataFormInfo={dataFormInfo}
                                        styles={props.section.styles}
                                        total={itemTotal}
                                        formula={itemTotal.formula}
                                    />
                                );
                            })}
                        </DataTableRow>
                    ))}

                    {section.indicatorsPosition === "end" &&
                        section.indicators.map(indicator => {
                            return (
                                <RowIndicatorItem
                                    key={`parent_${indicator.id}`}
                                    indicator={indicator}
                                    colSpan="0"
                                    dataFormInfo={dataFormInfo}
                                    periods={[section.dataEntryPeriod?.id || dataFormInfo.period]}
                                />
                            );
                        })}
                </TableBody>
            </DataTable>
        </DataTableSection>
    );
});

const useStyles = makeStyles({
    wrapper: { margin: 10 },
    header: { fontSize: "1.4em", fontWeight: "bold" as const },
    center: { display: "table", margin: "0 auto" },
    tableStyles: { backgroundColor: "aqua !important" },
});

export default React.memo(TableForm);
