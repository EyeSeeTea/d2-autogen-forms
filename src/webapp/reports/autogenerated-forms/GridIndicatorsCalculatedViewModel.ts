import _ from "lodash";
import { DataElement } from "../../../domain/common/entities/DataElement";
import { Section, SectionWithIndicatorsCalculated, Texts } from "../../../domain/common/entities/DataForm";
import { Maybe } from "../../../utils/ts-utils";
import { DataFormInfo } from "./AutogeneratedForm";
import i18n from "../../../locales";
import { Code } from "../../../domain/common/entities/Base";

export interface GridCalculatedModel {
    id: string;
    name: string;
    periods: string[];
    toggle: Section["toggle"];
    toggleMultiple: Section["toggleMultiple"];
    texts: Texts;
    tables: GroupedTable[];
}

type ParseSection = {
    rowName?: string;
    columnName?: string;
};

type SubSection = {
    rowName: string;
    columnName: string;
    dataElement: Maybe<DataElement>;
    indicatorCode: Maybe<Code>;
};

type TableCell = {
    columnName: string;
    dataElement: Maybe<DataElement>;
    disabled: boolean;
    defaultValue: Maybe<string>;
    formula: Maybe<{ formula: string; dataElementCodes: string[] }>;
};

type TableRow = {
    label: string;
    cells: TableCell[];
};

type GroupedTable = {
    rowName: string;
    columns: string[];
    rows: TableRow[];
};

const separator = " - ";

const DEFAULT_VALUE_NO_DENOMINATOR = "1";

export class GridIndicatorsCalculatedViewModel {
    static get(section: SectionWithIndicatorsCalculated, dataFormInfo: DataFormInfo): GridCalculatedModel {
        const subSections = _(section.dataElements)
            .map((dataElement): Maybe<SubSection> => {
                const parsedSection = this.parseSections(dataElement.name);
                if (!parsedSection) return undefined;

                // const indicatorCode = section.indicators.find(
                //     indicator => indicator.name.toLowerCase() === parsedSection.rowName?.toLowerCase()
                // )?.code;

                return {
                    rowName: parsedSection.rowName ?? "",
                    columnName: parsedSection.columnName ?? "",
                    dataElement: dataElement,
                    indicatorCode: undefined,
                };
            })
            .compact()
            .value();

        const subSectionsByRowName = _(subSections)
            .groupBy(subSection => subSection.rowName)
            .value();

        const tables = Object.keys(subSectionsByRowName)
            .sort()
            .map((rowName): GroupedTable => {
                const items = subSectionsByRowName[rowName] ?? [];

                const indicatorCode = section.indicators.find(
                    indicator => indicator.name.toLowerCase() === rowName.toLowerCase()
                )?.code;

                const rowConfig = section.rows.find(item => item.code === indicatorCode);

                const columnOrder = _(items)
                    .map(x => x.columnName)
                    .uniq()
                    .value();

                // TODO: Make inserting new columns configurable
                const columnsWithDenominator = this.insertItemAt(columnOrder, 1, "denominator");
                const columnsWithValue = this.insertItemAt(columnsWithDenominator, 2, "value");

                const cells = columnsWithValue.map((col): TableCell => {
                    if (col === "denominator") {
                        const dataElementDenominator = rowConfig?.denominator
                            ? dataFormInfo.metadata.dataForm.dataElements.find(
                                  de => de.code === rowConfig.denominator?.dataElementCode
                              )
                            : undefined;

                        const columnName = _(dataElementDenominator?.name).split(separator).first();
                        return {
                            disabled: true,
                            columnName: columnName || i18n.t("Denominator"),
                            dataElement: dataElementDenominator,
                            defaultValue: DEFAULT_VALUE_NO_DENOMINATOR,
                            formula: undefined,
                        };
                    }

                    if (col === "value") {
                        return {
                            disabled: true,
                            columnName: i18n.t("Value"),
                            dataElement: undefined,
                            defaultValue: undefined,

                            formula: rowConfig?.value
                                ? {
                                      formula: rowConfig.value.formula.value,
                                      dataElementCodes: rowConfig.value.dataElementCodes,
                                  }
                                : undefined,
                        };
                    }

                    const subSection = items.find(x => x.columnName === col);

                    return {
                        disabled: subSection?.dataElement === undefined,
                        columnName: col,
                        dataElement: subSection?.dataElement,
                        defaultValue: undefined,
                        formula: undefined,
                    };
                });

                const allColumns = _(cells)
                    .map(cell => cell.columnName)
                    .value();

                const indicatorRow: TableRow = { label: rowName, cells };
                return { columns: allColumns, rowName, rows: [indicatorRow] };
            });

        return {
            id: section.id,
            name: section.name,
            periods: section.periods,
            texts: section.texts,
            toggle: section.toggle,
            toggleMultiple: section.toggleMultiple,
            tables: tables,
        };
    }

    private static parseSections(name: string): Maybe<ParseSection> {
        const parts = name.split(separator);

        if (parts.length !== 2) return undefined;

        return { rowName: parts[0]?.trim(), columnName: parts[1]?.trim() };
    }

    private static insertItemAt<T>(items: T[], index: number, value: T): T[] {
        if (!Number.isInteger(index) || index < 0 || index > items.length) {
            throw new RangeError(`Index ${index} out of bounds [0, ${items.length}]`);
        }
        return [...items.slice(0, index), value, ...items.slice(index)];
    }
}
