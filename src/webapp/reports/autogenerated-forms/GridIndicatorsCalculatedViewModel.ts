import _ from "lodash";
import { DataElement } from "../../../domain/common/entities/DataElement";
import { Section, SectionWithIndicatorsCalculated, Texts } from "../../../domain/common/entities/DataForm";
import { Maybe } from "../../../utils/ts-utils";
import { DataFormInfo } from "./AutogeneratedForm";
import { Period } from "../../../domain/common/entities/Period";

export interface GridCalculatedModel {
    id: string;
    name: string;
    periods: Period[];
    toggle: Section["toggle"];
    toggleMultiple: Section["toggleMultiple"];
    texts: Texts;
    tables: GroupedTable[];
    hidden: boolean;
}

type ParseSection = {
    rowName?: string;
    columnName?: string;
};

type SubSection = {
    rowName: string;
    columnName: string;
    dataElement: DataElement;
};

type TableCell = {
    columnName: string;
    dataElement: Maybe<DataElement>;
    disabled: boolean;
    defaultValue: Maybe<string>;
    formula: Maybe<{ formula: string; dataElementCodes: string[] }>;
};

type TableRow = {
    label: string;
    cells: TableCell[];
};

type GroupedTable = {
    rowName: string;
    columns: string[];
    rows: TableRow[];
};

const separator = " - ";
const columnSeparator = "|.|";

type Item = {
    name: string;
    position: number;
};

export class GridIndicatorsCalculatedViewModel {
    private static getVirtualSubSections(
        section: SectionWithIndicatorsCalculated,
        dataFormInfo: DataFormInfo
    ): SubSection[] {
        return _(section.virtualRows)
            .map(row => {
                const dataElement = dataFormInfo.metadata.dataForm.dataElements.find(
                    de => de.code === row.dataElementCode
                );
                if (!dataElement) return undefined;
                const parsedSection = this.parseSections(row.rowName);

                return {
                    rowName: parsedSection?.rowName ?? "",
                    columnName: parsedSection?.columnName ?? "",
                    dataElement: dataElement,
                };
            })
            .compact()
            .value();
    }

    static get(section: SectionWithIndicatorsCalculated, dataFormInfo: DataFormInfo): GridCalculatedModel {
        const subSections = _(section.dataElements)
            .map((dataElement): Maybe<SubSection> => {
                const parsedSection = this.parseSections(dataElement.name);
                if (!parsedSection) return undefined;

                return {
                    rowName: parsedSection.rowName ?? "",
                    columnName: parsedSection.columnName ?? "",
                    dataElement: dataElement,
                };
            })
            .compact()
            .value();

        const virtualSubSections = this.getVirtualSubSections(section, dataFormInfo);

        const subSectionsByRowName = _([...virtualSubSections, ...subSections])
            .groupBy(subSection => subSection.rowName)
            .value();

        const tables = Object.keys(subSectionsByRowName).map((rowName): GroupedTable => {
            const items = subSectionsByRowName[rowName];

            const allDataElementCodes = items?.map(i => i.dataElement.code) ?? [];

            const vc = section.virtualColumns.filter(vc => allDataElementCodes.includes(vc.dataElementCode));

            const columnOrder = _(items)
                .map(x => `${x.columnName}${columnSeparator}${x.dataElement.code}`)
                .uniq()
                .value();

            const columnsToInsertPosition = vc.map(vcItem => {
                return {
                    position: vcItem.position,
                    name: `${vcItem.columnName}${columnSeparator}${vcItem.dataElementCode}`,
                };
            });

            const columnsToRender = this.insertItemsIntoArray(columnsToInsertPosition, columnOrder);

            const cells = columnsToRender.map((col): TableCell => {
                const [columnName, dataElementCode] = col.split(columnSeparator);
                const virtualColumn = section.virtualColumns.find(
                    vc => vc.dataElementCode === dataElementCode && vc.columnName === columnName
                );

                if (virtualColumn) {
                    switch (virtualColumn.type) {
                        case "calculated":
                            return {
                                disabled: false,
                                columnName: virtualColumn.columnName,
                                dataElement: undefined,
                                defaultValue: undefined,
                                formula: {
                                    dataElementCodes: virtualColumn.formula.dataElementCodes,
                                    formula: virtualColumn.formula.value,
                                },
                            };
                        case "dataElement": {
                            const dataElement = dataFormInfo.metadata.dataForm.dataElements.find(
                                de => de.code === virtualColumn.dataElementRefValue
                            );

                            return {
                                disabled: dataElement === undefined,
                                columnName: dataElement?.name ?? "",
                                dataElement: dataElement,
                                defaultValue: undefined,
                                formula: undefined,
                            };
                        }
                    }
                } else {
                    const subSection = items?.find(x => x.columnName === columnName);
                    const isInVirtual = section.virtualRows.some(vr => vr.dataElementCode === dataElementCode);

                    return {
                        disabled: subSection?.dataElement === undefined || isInVirtual,
                        columnName: columnName ?? "",
                        dataElement: subSection?.dataElement,
                        defaultValue: undefined,
                        formula: undefined,
                    };
                }
            });

            const allColumns = _(cells)
                .map(cell => cell.columnName)
                .value();

            const indicatorRow: TableRow = { label: rowName, cells };
            return { columns: allColumns, rowName, rows: [indicatorRow] };
        });

        return {
            id: section.id,
            name: section.name,
            periods: section.periods,
            texts: section.texts,
            toggle: section.toggle,
            toggleMultiple: section.toggleMultiple,
            tables: tables,
            hidden: section.hidden || false,
        };
    }

    private static parseSections(name: string): Maybe<ParseSection> {
        const parts = name.split(separator);

        if (parts.length !== 2) return undefined;

        return { rowName: parts[0]?.trim(), columnName: parts[1]?.trim() };
    }

    private static insertItemsIntoArray(itemsToInsert: Item[], targetArray: string[]): string[] {
        // Crea una copia del arreglo para no mutar el original
        const newArray = [...targetArray];

        // Ordena los items por posición para evitar problemas de índices
        // al insertar, ya que la inserción desplaza los elementos.
        const sortedItems = itemsToInsert.sort((a, b) => a.position - b.position);

        // Itera sobre los items ordenados e inserta cada uno en su posición
        sortedItems.forEach(item => {
            // Inserta el 'name' en la 'position' del nuevo arreglo.
            newArray.splice(item.position, 0, item.name);
        });

        return newArray;
    }
}
