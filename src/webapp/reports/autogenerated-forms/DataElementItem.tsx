import _ from "lodash";
import { makeStyles } from "@material-ui/core";
import React from "react";
import { DataElement } from "../../../domain/common/entities/DataElement";
import { DataFormInfo } from "./AutogeneratedForm";
import { CommentIcon } from "./CommentIcon";
import DataEntryItem, { DataEntryItemProps } from "./DataEntryItem";
import { DataValue } from "../../../domain/common/entities/DataValue";
import { Row } from "./GridWithTotalsViewModel";
import { isDev } from "../../..";
import { Maybe } from "../../../utils/ts-utils";

export interface DataElementItemProps {
    dataElement: DataElement;
    dataFormInfo: DataFormInfo;
    period?: string; // Override period in dataFormInfo
    onChange?(dataValue: DataValue): void;
    noComment?: boolean;
    manualyDisabled?: boolean;
    total?: DataElement;
    columnTotal?: DataElement;
    columnDataElements?: DataElement[];
    rows?: Row[];
    rowName?: string;
}

export const DataElementItem: React.FC<DataElementItemProps> = React.memo(props => {
    const {
        dataElement,
        dataFormInfo,
        period,
        onChange,
        noComment,
        manualyDisabled,
        total,
        columnTotal,
        columnDataElements,
        rows,
        rowName,
    } = props;

    const classes = useStyles();
    const elId = _([dataElement.id, period]).compact().join("-");
    const dataElementCocId = dataElement.cocId ?? dataFormInfo.categoryOptionComboId;
    const auditId = _([dataElement.orgUnit, dataElement.id, dataElementCocId, "val"]).compact().join("-");

    const notifyParent = React.useCallback<DataEntryItemProps["onValueChange"]>(
        async dataValue => {
            if (onChange) onChange(dataValue);
            return dataValue;
        },
        [onChange]
    );

    const onDoubleClick = () => {
        if (!isDev) {
            const dhis2: any = (window as any).dhis2;
            const de = dhis2 && dhis2.de;
            const hasGetSelected = de && typeof de.getSelectedPeriod === "function";
            const originalGetSelectedPeriod = hasGetSelected ? de.getSelectedPeriod : undefined;
            const periodInput = window.document.getElementById("selectedPeriodId") as HTMLInputElement | null;
            const previousDomValue = periodInput ? periodInput.value : undefined;

            try {
                if (de && period) {
                    if (hasGetSelected) de.getSelectedPeriod = () => ({ iso: period });
                    de.currentPeriod = period;
                    de.currentPeriodId = period;
                    de.currentPeriodIso = period;
                    if (typeof de.setSelectedPeriod === "function") {
                        try {
                            de.setSelectedPeriod({ iso: period });
                        } catch (_) {
                            // ignore
                        }
                    }
                    if (periodInput) periodInput.value = period;
                }
                window.viewHist(dataElement.id, dataElementCocId);
            } finally {
                try {
                    if (de && hasGetSelected && originalGetSelectedPeriod)
                        de.getSelectedPeriod = originalGetSelectedPeriod;
                    if (periodInput && typeof previousDomValue !== "undefined") periodInput.value = previousDomValue;
                } catch (_) {
                    // noop
                }
            }
        }
    };

    const onClick = (dataElement: DataElement, rowName: Maybe<string>) => {
        if (!isDev && rowName) {
            const topBarDeNameEl = window.document.querySelector("#currentDataElement");
            if (topBarDeNameEl) {
                topBarDeNameEl.textContent = `${rowName} ${dataElement.name}`;
            }
        }
    };

    const onBlur = () => {
        if (!isDev) {
            const topBarDeNameEl = window.document.querySelector("#currentDataElement");
            if (topBarDeNameEl) {
                topBarDeNameEl.textContent = "No Data Element Selected";
            }
        }
    };

    return !noComment ? (
        <div id={elId} className={classes.valueWrapper}>
            <div
                onClick={() => onClick(dataElement, rowName)}
                onBlur={() => onBlur()}
                className={`${classes.valueInput} sourcetype`}
                id={auditId}
            >
                <DataEntryItem
                    dataElement={dataElement}
                    dataFormInfo={dataFormInfo}
                    period={period}
                    onValueChange={notifyParent}
                    manualyDisabled={manualyDisabled}
                    total={total}
                    columnTotal={columnTotal}
                    columnDataElements={columnDataElements}
                    cocId={dataElementCocId}
                    rows={rows}
                />
            </div>
            <CommentIcon
                dataElementId={dataElement.id}
                categoryOptionComboId={dataElementCocId}
                period={period}
            />
        </div>
    ) : (
        <div id={elId} className={classes.valueWrapper}>
            <div
                onClick={() => onClick(dataElement, rowName)}
                onBlur={() => onBlur()}
                onDoubleClick={() => onDoubleClick()}
                className={`${classes.valueInput} entryfield2`}
                id={auditId}
            >
                <DataEntryItem
                    dataElement={dataElement}
                    dataFormInfo={dataFormInfo}
                    period={period}
                    onValueChange={notifyParent}
                    manualyDisabled={manualyDisabled}
                    total={total}
                    columnTotal={columnTotal}
                    columnDataElements={columnDataElements}
                    cocId={dataElementCocId}
                />
            </div>
        </div>
    );
});

const useStyles = makeStyles({
    valueInput: { flexGrow: 1, border: "0 !important" },
    valueWrapper: { display: "flex" },
});
