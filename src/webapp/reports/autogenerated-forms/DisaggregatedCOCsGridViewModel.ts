import _ from "lodash";
import { Section, Texts } from "../../../domain/common/entities/DataForm";
import { DataFormInfo } from "./AutogeneratedForm";
import { DataElement } from "../../../domain/common/entities/DataElement";
import { Indicator } from "../../../domain/common/entities/Indicator";
import { getDescription } from "../../../utils/viewTypes";
import { Maybe } from "../../../utils/ts-utils";
import { getDataElementLabel } from "./GridFormViewModel";

export class DisaggregatedCOCsGridViewModel {
    static get(section: Section, dataFormInfo: DataFormInfo): Grid {
        const columns = DisaggregatedCOCsGridViewModel.getGridColumns(section, dataFormInfo);
        const rows = DisaggregatedCOCsGridViewModel.getGridRows(section, columns);
        const summary = DisaggregatedCOCsGridViewModel.getSummary(section, dataFormInfo, columns);

        return {
            id: section.id,
            indicators: section.indicators,
            name: section.name,
            columns: columns,
            rows: rows,
            showIndex: section.showIndex,
            tabs: section.tabs,
            toggle: section.toggle,
            toggleMultiple: section.toggleMultiple,
            texts: section.texts,
            summary: section.totals ? summary : [],
        };
    }

    private static getGridColumns(section: Section, dataFormInfo: DataFormInfo): Column[] {
        const columns = section.dataElements.map(dataElement => {
            const columnItems = _(dataElement.categoryOptionCombos)
                .map(coc => {
                    const columnName = getColumnNameFromCoc(coc) || "";

                    return {
                        name: columnName,
                        description: getDescription(section.columnsDescriptions, dataFormInfo, columnName),
                    };
                })
                .uniqBy(column => column.name)
                .value();

            return {
                columnItems: sortItems(columnItems),
                dataElement: { ...dataElement, name: getDataElementLabel(section, dataElement) },
            };
        });

        return columns;
    }

    private static getGridRows(section: Section, columns: Column[]): Row[] {
        const rows = _(section.dataElements)
            .flatMap(dataElement => dataElement.categoryOptionCombos)
            .groupBy(coc => getRowNameFromCoc(coc))
            .map((_, rowName) => {
                if (!rowName) {
                    console.warn("Skipping row with empty row name");
                    return undefined;
                }

                const items = section.dataElements
                    .filter(dataElement =>
                        dataElement.categoryOptionCombos.some(coc => getRowNameFromCoc(coc) === rowName)
                    )
                    .map(dataElement => {
                        const rowItems = dataElement.categoryOptionCombos
                            .filter(item => getRowNameFromCoc(item) === rowName)
                            .map(coc => {
                                const columnName =
                                    columns
                                        .find(column => column.dataElement.id === dataElement.id)
                                        ?.columnItems.find(columnItem => columnItem.name === getColumnNameFromCoc(coc))
                                        ?.name || "";

                                return {
                                    ...dataElement,
                                    cocId: coc.id,
                                    categoryOptionCombos: [coc],
                                    categoryCombos: {
                                        ...dataElement.categoryCombos,
                                        categoryOptionCombos: [{ ...coc, formName: coc.formName }],
                                    },
                                    columnName: columnName,
                                };
                            });

                        const disaggregatedDataElement = {
                            ...dataElement,
                            categoryCombos: {
                                ...dataElement.categoryCombos,
                                categoryOptionCombos: dataElement.categoryCombos.categoryOptionCombos.filter(
                                    coc => getRowNameFromCoc(coc) === rowName
                                ),
                            },
                            categoryOptionCombos: dataElement.categoryOptionCombos.filter(
                                coc => getRowNameFromCoc(coc) === rowName
                            ),
                        };

                        return {
                            dataElement: disaggregatedDataElement,
                            rowItems: rowItems,
                        };
                    });

                return {
                    name: rowName,
                    items: items,
                };
            })
            .compact()
            .value();

        return sortItems(rows);
    }

    private static getSummary(section: Section, dataFormInfo: DataFormInfo, columns: Column[]): Summary[] {
        const allDataElements = dataFormInfo.metadata.dataForm.dataElements;

        const summary = _(section.totals)
            .map((sectionTotal, key) => {
                const selectedDataElements = allDataElements.filter(dataElement =>
                    sectionTotal.dataElementsCodes.includes(dataElement.code)
                );

                const cells = selectedDataElements.map(dataElement => {
                    const columnDE = columns.find(column => column.dataElement.id === dataElement.id);
                    const columnItems = columnDE ? columnDE.columnItems : [];

                    return {
                        ...dataElement,
                        columnItems: columnItems,
                    };
                });

                return {
                    cellName: key,
                    cells: cells,
                };
            })
            .value();

        return summary;
    }
}

export type Grid = {
    id: string;
    name: string;
    columns: Column[];
    rows: Row[];
    showIndex: Section["showIndex"];
    tabs: Section["tabs"];
    toggle: Section["toggle"];
    toggleMultiple: Section["toggleMultiple"];
    texts: Texts;
    summary: Summary[];
    indicators: Indicator[];
};

export type ColumnItem = {
    name: string;
    description?: string;
};

type Column = {
    dataElement: DataElement;
    columnItems: ColumnItem[];
};

type Row = {
    name: string;
    items: { dataElement: DataElement; rowItems: Array<DataElement & { columnName: string }> }[];
};

type Summary = {
    cellName: string;
    cells: Array<DataElement & { columnItems: ColumnItem[] }>;
};

const separator = ", ";

const sortItems = <T extends { name: string }>(items: T[]): T[] => {
    return _(items)
        .sortBy(item => {
            const match = item.name.match(/^(\d+)(?:\D|$)/);
            const numericValue = match ? match[1] : undefined;
            const parsedNumericValue = numericValue ? parseInt(numericValue, 10) : undefined;

            return [parsedNumericValue === undefined ? 1 : 0, parsedNumericValue ?? item.name.toLowerCase()];
        })
        .value();
};

function getColumnNameFromCoc(categoryOptionCombo: { name: string }): Maybe<string> {
    return categoryOptionCombo.name.split(separator)[0];
}

function getRowNameFromCoc(categoryOptionCombo: { name: string }): Maybe<string> {
    return categoryOptionCombo.name.split(separator)[1];
}
