import { makeStyles } from "@material-ui/core";
import React from "react";
import { Section } from "../../../domain/common/entities/DataForm";
import { DataValue } from "../../../domain/common/entities/DataValue";
import { DataFormInfo } from "./AutogeneratedForm";
import { DataElementItem } from "./DataElementItem";
import { evaluateTotalRule, verifyConditionByDataValueType } from "./DataEntryItem";
import { Html } from "./Html";
import { Id } from "../../../domain/common/entities/Base";
import { ToggleDataElement } from "../../../domain/common/entities/ToggleMultiple";

export interface DataTableProps {
    section: DataTableSectionObj;
    dataFormInfo: DataFormInfo;
    children: React.ReactNode;
    sectionStyles?: Section["styles"];
}

interface DataTableSectionObj {
    id: Id;
    name: string;
    titleVariant?: Section["titleVariant"];
    texts: Section["texts"];
    toggle: Section["toggle"];
    toggleMultiple: Section["toggleMultiple"];
}

const DataTableSection: React.FC<DataTableProps> = React.memo(props => {
    const { section, children, dataFormInfo, sectionStyles } = props;
    const sectionTotalRules = dataFormInfo.metadata.dataForm.totalRules.sectionTotalRules.filter(sectionTotalRule =>
        sectionTotalRule.sections.includes(section.id)
    );
    const { toggle, toggleMultiple } = section;
    const classes = useStyles();

    const isSectionDisabled = React.useMemo(() => {
        if (toggle.type === "none") return false;
        return toggle.disabled;
    }, [toggle]);

    const isSectionOpen = React.useMemo(() => {
        if (toggle.type === "dataElement") {
            const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
            return isDataValueEnabled(dataValue);
        } else if (toggle.type === "dataElementExternal") {
            const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
            return verifyConditionByDataValueType(dataValue, toggle);
        } else {
            return true;
        }
    }, [toggle, dataFormInfo]);

    const isSectionVisible = React.useMemo(() => {
        if (!toggleMultiple) return true;
        const { logicalOperator, toggleDataElements } = toggleMultiple;

        const evaluateToggleCondition = (toggle: ToggleDataElement) => {
            const dataValue = dataFormInfo.data.values.getOrEmpty(toggle.dataElement, dataFormInfo);
            return verifyConditionByDataValueType(dataValue, toggle);
        };

        return logicalOperator === "OR"
            ? toggleDataElements.some(toggle => evaluateToggleCondition(toggle))
            : toggleDataElements.every(toggle => evaluateToggleCondition(toggle));
    }, [dataFormInfo, toggleMultiple]);

    const isSectionVisibleFromTotals = React.useMemo(() => {
        return sectionTotalRules
            .map(sectionTotalRule => evaluateTotalRule(sectionTotalRule, dataFormInfo))
            .some(value => value);
    }, [dataFormInfo, sectionTotalRules]);

    const titleStyle = section.titleVariant ? `${classes.title} ${classes[section.titleVariant]}` : classes.title;

    if (toggle.type === "dataElementExternal" && !isSectionOpen) return null;
    if (toggleMultiple?.toggleDataElements.length !== 0 && !isSectionVisible) return null;
    if (sectionTotalRules.length !== 0 && !isSectionVisibleFromTotals) return null;

    return (
        <div className={classes.wrapper}>
            <div style={{ backgroundColor: sectionStyles?.title.backgroundColor }}>
                <h3 className={titleStyle}>{section.name}</h3>
            </div>

            <Html backgroundColor={sectionStyles?.title.backgroundColor} content={section.texts.header} />

            {toggle.type === "dataElement" && (
                <div className={classes.toggleWrapper}>
                    <div className={classes.toggleTitle}>{toggle.dataElement.name}</div>
                    <DataElementItem dataElement={toggle.dataElement} dataFormInfo={dataFormInfo} />
                </div>
            )}

            {(isSectionOpen || isSectionDisabled) && (
                <>
                    {children}
                    <Html content={section.texts.footer} />
                </>
            )}
        </div>
    );
});

const useStyles = makeStyles({
    wrapper: { margin: 10, border: "1px solid black" },
    toggleWrapper: { margin: 10 },
    toggleTitle: { marginBottom: 10 },
    title: { textAlign: "center", margin: 0, padding: "1em" },
    subtitle: { textAlign: "center", marginBottom: 10 },
    h1: {
        fontSize: "6rem !important",
        margin: "18px !important",
    },
    h2: {
        fontSize: "3.75rem !important",
        margin: "18px !important",
    },
    h3: {
        fontSize: "3rem !important",
        margin: "18px !important",
    },
    h4: {
        fontSize: "2.125rem !important",
        margin: "18px !important",
    },
    h5: {
        fontSize: "1.5rem !important",
        margin: "18px !important",
    },
    h6: {
        fontSize: "1.25rem !important",
        margin: "18px !important",
    },
});

function isDataValueEnabled(dataValue: DataValue): boolean {
    return dataValue.type === "BOOLEAN" ? Boolean(dataValue.value) : false;
}

export default React.memo(DataTableSection);
