import React from "react";
import { SectionWithPeriods } from "../../../domain/common/entities/DataForm";
import { DataFormInfo } from "./AutogeneratedForm";
import { makeStyles } from "@material-ui/core";
import { getFilteredIndicators, GridWithCatOptionCombosViewModel } from "./GridWithCatOptionCombosViewModel";
import DataTableSection from "./DataTableSection";
import {
    DataTable,
    DataTableRow,
    TableBody,
    TableHead,
    // @ts-ignore
} from "@dhis2/ui";
import { DataElementItem } from "./DataElementItem";
import { CustomDataTableCell, CustomDataTableColumnHeader } from "./datatables/CustomDataTables";
import { DataTableCellFormula } from "./datatables/DataTableCellFormula";
import { DataTableCellRowTotal } from "./datatables/DataTableCellRowTotal";
import { DataTableCellSummary } from "./datatables/DataTableCellSummary";
import { DataTableCellRowName } from "./datatables/DataTableCellRowName";
import { Html } from "./Html";
import { IndicatorItem, RowIndicatorItem } from "../../components/IndicatorItem/IndicatorItem";
import { checkIndicatorDirection } from "../../../domain/common/entities/Indicator";
import i18n from "../../../locales";

export interface GridWithCatOptionCombosProps {
    dataFormInfo: DataFormInfo;
    section: SectionWithPeriods;
}

/*
 * Convert data forms into the default data entry form, using the data elements and category option combinations. 
An example for section ITNs:
 *
 *          Data element                                Category option combos
 *    - ITNs - Basic                            [Public, Private, Community, Combined]
 *    - ITNs - Extended - Written Policy        [Public, Private, Community, Combined]
 *    - ITNs - Extended - Policy Implemented    [Public, Private, Community, Combined]
 *    - ITNs - Extended - Policy Extra          [Public, Private, Community, Combined, default]
 *
 *    This will create this table:
 *
 *
 *                                           |  Public  |  Private  |  Combined  |  Community  |  default
 *    ----------------------------------------------------------------------------------------------------
 *    ITNs - Basic                           |          |           |            |             |
 *                     | Written Policy      |          |           |            |             |
 *    ITNs - Extended  | Policy Implemented  |          |           |            |             |
 *                     | Policy Extra        |          |           |            |             |
 **/

const GridWithCatOptionCombos: React.FC<GridWithCatOptionCombosProps> = props => {
    const { dataFormInfo, section } = props;
    const classes = useStyles();
    const grid = React.useMemo(
        () => GridWithCatOptionCombosViewModel.get(section, dataFormInfo),
        [section, dataFormInfo]
    );

    const showRowTotals = section.showRowTotals;
    const showIndicatorsAfter = section.indicators.some(indicator => checkIndicatorDirection(indicator, "after"));
    const showIndicatorsBefore = section.indicators.some(indicator => checkIndicatorDirection(indicator, "before"));
    const nonDirectionalIndicators = section.indicators.filter(
        indicator => !checkIndicatorDirection(indicator, "before") && !checkIndicatorDirection(indicator, "after")
    );

    return (
        <DataTableSection section={grid} dataFormInfo={dataFormInfo} sectionStyles={section.styles}>
            <DataTable className={classes.table} layout="fixed" width="initial">
                <TableHead>
                    <DataTableRow>
                        <CustomDataTableColumnHeader
                            backgroundColor={section.styles.columns.backgroundColor}
                            width="400px"
                            colSpan={`${showIndicatorsBefore ? "3" : "2"}`}
                        ></CustomDataTableColumnHeader>

                        {grid.periods.length > 0 && (
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                width="50px"
                            >
                                {i18n.t("Period")}
                            </CustomDataTableColumnHeader>
                        )}

                        {grid.columns.map(column => (
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                key={column.name}
                            >
                                <div className={classes.header}>
                                    <span>{column.name}</span>
                                    <span
                                        className={classes.description}
                                        dangerouslySetInnerHTML={{ __html: column.description || "" }}
                                    ></span>
                                </div>
                            </CustomDataTableColumnHeader>
                        ))}

                        {showIndicatorsAfter && (
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                key="column-indicators"
                            ></CustomDataTableColumnHeader>
                        )}

                        {showRowTotals && (
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                key="column-row-totals"
                            >
                                <div dangerouslySetInnerHTML={{ __html: grid.texts.rowTotals || "" }}></div>
                            </CustomDataTableColumnHeader>
                        )}
                    </DataTableRow>
                </TableHead>

                <TableBody>
                    {grid.rows.map(row => {
                        const { groupDescription, groupName, rows } = row;

                        return rows.map((row, idx) => (
                            <DataTableRow key={`${groupName}-${idx}`}>
                                {groupName ? (
                                    <>
                                        {idx === 0 && (
                                            <CustomDataTableCell
                                                backgroundColor={section.styles.rows.backgroundColor}
                                                rowSpan={rows.length.toString()}
                                            >
                                                <span className={classes.rowTitle}>{groupName}</span>
                                                <Html content={groupDescription} />
                                            </CustomDataTableCell>
                                        )}

                                        <CustomDataTableCell backgroundColor={section.styles.rows.backgroundColor}>
                                            <DataTableCellRowName html={row.dataElement.htmlText} name={row.deName} />
                                        </CustomDataTableCell>
                                    </>
                                ) : (
                                    <CustomDataTableCell
                                        backgroundColor={section.styles.rows.backgroundColor}
                                        colSpan="2"
                                    >
                                        <DataTableCellRowName html={row.dataElement.htmlText} name={row.deName} />
                                    </CustomDataTableCell>
                                )}

                                {getFilteredIndicators(section.indicators, row, "before").map(
                                    indicator =>
                                        indicator && (
                                            <IndicatorItem
                                                key={`${indicator.id}-${row.dataElement.code}`}
                                                indicator={indicator}
                                                dataFormInfo={dataFormInfo}
                                                periods={[dataFormInfo.period]}
                                            />
                                        )
                                )}

                                {row.period && (
                                    <CustomDataTableCell backgroundColor={section.styles.rows.backgroundColor}>
                                        <span>{row.period.label}</span>
                                    </CustomDataTableCell>
                                )}

                                {grid.columns.map(column => {
                                    const dataElement = column.dataElements.find(de => de.name === row.name);
                                    return dataElement ? (
                                        <CustomDataTableCell
                                            backgroundColor={section.styles.rows.backgroundColor}
                                            key={[dataElement.id, column.name].join("-")}
                                        >
                                            <DataElementItem
                                                dataElement={dataElement}
                                                dataFormInfo={dataFormInfo}
                                                noComment={dataElement.disabledComments}
                                                period={row.period?.id || dataFormInfo.period}
                                                lockException={row.period ? true : false}
                                            />
                                        </CustomDataTableCell>
                                    ) : (
                                        <CustomDataTableCell
                                            backgroundColor={section.styles.rows.backgroundColor}
                                            key={`cell-${idx}-${column.name}`}
                                        ></CustomDataTableCell>
                                    );
                                })}

                                {getFilteredIndicators(section.indicators, row, "after").map(
                                    indicator =>
                                        indicator && (
                                            <IndicatorItem
                                                key={`${indicator.id}-${row.dataElement.code}`}
                                                indicator={indicator}
                                                dataFormInfo={dataFormInfo}
                                                periods={[dataFormInfo.period]}
                                            />
                                        )
                                )}

                                {showRowTotals && (
                                    <DataTableCellRowTotal
                                        dataFormInfo={dataFormInfo}
                                        styles={section.styles}
                                        dataElement={row.dataElement}
                                    />
                                )}
                            </DataTableRow>
                        ));
                    })}

                    {nonDirectionalIndicators.map(indicator => (
                        <RowIndicatorItem
                            key={`parent_${indicator.id}`}
                            indicator={indicator}
                            colSpan="2"
                            dataFormInfo={dataFormInfo}
                            periods={[dataFormInfo.period]}
                        />
                    ))}

                    {grid.summary.map(summary => (
                        <DataTableRow key={`${summary.cellName}-totals`}>
                            <CustomDataTableCell
                                colSpan={`${showIndicatorsBefore ? "3" : "2"}`}
                                backgroundColor={section.styles.totals.backgroundColor}
                                key="total-column-name"
                            >
                                <Html content={summary.cellName} />
                            </CustomDataTableCell>
                            {summary.cells.map(itemTotal => {
                                return (
                                    <DataTableCellFormula
                                        key={itemTotal.columnName}
                                        dataFormInfo={dataFormInfo}
                                        styles={section.styles}
                                        total={itemTotal}
                                        formula={itemTotal.formula}
                                    />
                                );
                            })}
                            {showRowTotals && (
                                <DataTableCellSummary
                                    dataFormInfo={dataFormInfo}
                                    styles={section.styles}
                                    dataElements={summary.cells}
                                />
                            )}
                        </DataTableRow>
                    ))}
                </TableBody>
            </DataTable>
        </DataTableSection>
    );
};

const useStyles = makeStyles({
    header: {
        fontSize: "1.2em",
        fontWeight: "bold",
        flexDirection: "column",
        textAlign: "center",
        display: "flex",
        padding: "4px",
    },
    description: { fontWeight: "normal", fontSize: "0.8em" },
    table: { borderWidth: "3px !important", minWidth: "100%" },
    rowTitle: { fontSize: "1.2em", fontWeight: "bold" },
});

export default React.memo(GridWithCatOptionCombos);
