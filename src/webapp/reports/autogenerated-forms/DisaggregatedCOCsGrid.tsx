import React, { useCallback } from "react";
import { SectionDisaggregatedCocs } from "../../../domain/common/entities/DataForm";
import { DataFormInfo } from "./AutogeneratedForm";
import { makeStyles } from "@material-ui/core";
import DataTableSection from "./DataTableSection";
import {
    DataTable,
    DataTableRow,
    TableBody,
    TableHead,
    // @ts-ignore
} from "@dhis2/ui";
import { DataElementItem } from "./DataElementItem";
import { CustomDataTableCell, CustomDataTableColumnHeader } from "./datatables/CustomDataTables";
import { ColumnItem, DisaggregatedCOCsGridViewModel, Grid } from "./DisaggregatedCOCsGridViewModel";
import { DataTableCellRowTotal } from "./datatables/DataTableCellRowTotal";
import { Html } from "./Html";
import { checkVisibleRule } from "./hooks/useApplyRules";
import { DataElement } from "../../../domain/common/entities/DataElement";
import { RowIndicatorItem } from "../../components/IndicatorItem/IndicatorItem";

type DisaggregatedCOCsGridProps = {
    dataFormInfo: DataFormInfo;
    section: SectionDisaggregatedCocs;
};

/*
    This view type converts data forms with disaggregated data elements into a grid format with one category represented in the columns and the other in the rows.
    An example using the section ITNs:

    Data element                                        Categories                                      Category option combos
    ITNs - By age group and gender                      Age group => [0-12, 12-18, 18-24, 25-49, 50+]   "0-12, Male", "0-12, Female", "12-18, Male", "12-18, Female", "18-24, Male", "18-24, Female", "25-49, Male", "25-49, Female", "50+, Male", "50+, Female"
                                                        Gender => [Male, Female]
    ITNs - By age group and gender (Policy Extra)       Age group => [0-12, 12-18, 18-24, 25-49, 50+]   "0-12, Male", "0-12, Female", "12-18, Male", "12-18, Female", "18-24, Male", "18-24, Female", "25-49, Male", "25-49, Female", "50+, Male", "50+, Female"
                                                        Gender => [Male, Female]

    The grouping is done by separating the category option combination names with a (", ").
    This will create this table:

            |   ITNs - By age group and gender                        |  ITNs - By age group and gender (Policy Extra)                         
    ---------------------------------------------------------------------------------------------------------------------------
            |   0-12    |   12-18   |   18-24   |   25-49   |   50+   |   0-12   |   12-18   |   18-24   |   25-49   |   50+   |  
    ---------------------------------------------------------------------------------------------------------------------------|    
    Female  |           |           |           |           |         |          |           |           |           |         |
    Male    |           |           |           |           |         |          |           |           |           |         |
*/

const DisaggregatedCOCsGrid: React.FC<DisaggregatedCOCsGridProps> = props => {
    const { dataFormInfo, section } = props;
    const classes = useStyles();
    const grid = React.useMemo(
        () => DisaggregatedCOCsGridViewModel.get(section, dataFormInfo),
        [section, dataFormInfo]
    );

    const showRowTotals = section.showRowTotals;
    const { getColSpan, isDataElementVisible } = useGridLayout(grid, dataFormInfo);

    const Indicators = grid.indicators.map(indicator => {
        return (
            <RowIndicatorItem
                key={`parent_${indicator.id}`}
                indicator={indicator}
                colSpan="0"
                dataFormInfo={dataFormInfo}
                periods={[dataFormInfo.period]}
            />
        );
    });

    return (
        <DataTableSection section={grid} dataFormInfo={dataFormInfo} sectionStyles={section.styles}>
            <DataTable className={classes.table} layout="fixed" width="initial">
                <TableHead>
                    <DataTableRow>
                        {grid.columns.map(({ columnItems, dataElement }, index) => (
                            <>
                                {index === 0 && (
                                    <CustomDataTableColumnHeader
                                        backgroundColor={section.styles.columns.backgroundColor}
                                        colSpan={1}
                                    ></CustomDataTableColumnHeader>
                                )}

                                {isDataElementVisible(dataElement) && (
                                    <CustomDataTableColumnHeader
                                        backgroundColor={section.styles.columns.backgroundColor}
                                        key={`column-${dataElement.id}`}
                                        colSpan={getColSpan(columnItems)}
                                    >
                                        <span className={classes.header}>{dataElement.name}</span>
                                    </CustomDataTableColumnHeader>
                                )}
                            </>
                        ))}
                    </DataTableRow>

                    <DataTableRow>
                        {grid.columns.map(({ columnItems, dataElement }, index) => (
                            <>
                                {index === 0 && (
                                    <CustomDataTableColumnHeader
                                        backgroundColor={section.styles.columns.backgroundColor}
                                        colSpan={1}
                                    ></CustomDataTableColumnHeader>
                                )}
                                {isDataElementVisible(dataElement) && (
                                    <>
                                        {columnItems.map(columnItem => (
                                            <CustomDataTableColumnHeader
                                                backgroundColor={section.styles.columns.backgroundColor}
                                                key={`${columnItem.name}-${dataElement.id}`}
                                            >
                                                <div className={classes.header}>
                                                    <span>{columnItem.name}</span>
                                                    <span
                                                        className={classes.description}
                                                        dangerouslySetInnerHTML={{
                                                            __html: columnItem.description || "",
                                                        }}
                                                    ></span>
                                                </div>
                                            </CustomDataTableColumnHeader>
                                        ))}
                                        {showRowTotals && (
                                            <CustomDataTableColumnHeader
                                                backgroundColor={section.styles.columns.backgroundColor}
                                                key={`column-row-totals-${dataElement.id}`}
                                            >
                                                <div className={classes.header}>
                                                    <div
                                                        dangerouslySetInnerHTML={{ __html: grid.texts.rowTotals || "" }}
                                                    ></div>
                                                </div>
                                            </CustomDataTableColumnHeader>
                                        )}
                                    </>
                                )}
                            </>
                        ))}
                    </DataTableRow>
                </TableHead>

                <TableBody>
                    {section.indicatorsConfig.position === "start" && Indicators}
                    {grid.rows.map(row => (
                        <DataTableRow key={row.name}>
                            <CustomDataTableCell backgroundColor={section.styles.rows.backgroundColor}>
                                <span>{row.name}</span>
                            </CustomDataTableCell>

                            {grid.columns
                                .filter(column => isDataElementVisible(column.dataElement))
                                .map(({ columnItems, dataElement }) => {
                                    const rowItemByDe = row.items.find(
                                        rowItem => rowItem.dataElement.id === dataElement.id
                                    );

                                    return (
                                        <>
                                            {columnItems.map((columnItem, colIndex) => {
                                                const rowItem = rowItemByDe?.rowItems?.find(
                                                    rowItem => rowItem.columnName === columnItem.name
                                                );

                                                return rowItem ? (
                                                    <CustomDataTableCell
                                                        backgroundColor={section.styles.rows.backgroundColor}
                                                        key={`${rowItem.id}-${rowItem.cocId}`}
                                                    >
                                                        <DataElementItem
                                                            dataElement={rowItem}
                                                            dataFormInfo={dataFormInfo}
                                                            noComment={rowItem.disabledComments}
                                                            manualyDisabled={rowItem.disabled}
                                                        />
                                                    </CustomDataTableCell>
                                                ) : (
                                                    <CustomDataTableCell
                                                        backgroundColor={section.styles.rows.backgroundColor}
                                                        key={`${columnItem.name}-${colIndex}`}
                                                    ></CustomDataTableCell>
                                                );
                                            })}

                                            {showRowTotals && (
                                                <DataTableCellRowTotal
                                                    dataFormInfo={dataFormInfo}
                                                    styles={section.styles}
                                                    dataElement={dataElement}
                                                    onlyCocIds={rowItemByDe?.cocIdsToSum}
                                                />
                                            )}
                                        </>
                                    );
                                })}
                        </DataTableRow>
                    ))}
                    {grid.summary.map(summary => (
                        <DataTableRow key={`${summary.cellName}-totals`}>
                            <CustomDataTableCell
                                backgroundColor={section.styles.totals.backgroundColor}
                                key="total-column-name"
                            >
                                <Html content={summary.cellName} />
                            </CustomDataTableCell>
                            {summary.cells.map(
                                cell =>
                                    isDataElementVisible(cell) && (
                                        <>
                                            <DataTableCellRowTotal
                                                key={cell.id}
                                                colSpan={cell.columnItems.length}
                                                dataFormInfo={dataFormInfo}
                                                styles={section.styles}
                                                dataElement={cell}
                                                onlyCocIds={cell.cocIdsToSum}
                                            />
                                            {showRowTotals && (
                                                <CustomDataTableCell
                                                    backgroundColor={section.styles.totals.backgroundColor}
                                                    key={`total-${cell.id}`}
                                                >
                                                    <span>{""}</span>
                                                </CustomDataTableCell>
                                            )}
                                        </>
                                    )
                            )}
                        </DataTableRow>
                    ))}
                    {section.indicatorsConfig.position === "end" && Indicators}
                </TableBody>
            </DataTable>
        </DataTableSection>
    );
};

const useStyles = makeStyles({
    header: {
        fontSize: "1.2em",
        fontWeight: "bold",
        flexDirection: "column",
        textAlign: "center",
        display: "flex",
        padding: "4px",
    },
    description: { fontWeight: "normal", fontSize: "0.8em" },
    table: { borderWidth: "3px !important", minWidth: "100%" },
    rowTitle: { fontSize: "1.2em", fontWeight: "bold" },
});

export default React.memo(DisaggregatedCOCsGrid);

type GridLayoutState = {
    getColSpan: (columnItems?: ColumnItem[]) => number;
    isDataElementVisible: (dataElement: DataElement) => boolean;
};

function useGridLayout(grid: Grid, dataFormInfo: DataFormInfo): GridLayoutState {
    const getColSpan = useCallback(
        (columnItems?: ColumnItem[]) =>
            columnItems ? columnItems.length + 1 : grid.columns.flatMap(column => column.columnItems).length,
        [grid.columns]
    );

    const isDataElementVisible = useCallback(
        dataElement =>
            checkVisibleRule({
                dataElement,
                dataFormInfo,
                period: dataFormInfo.period,
            }),
        [dataFormInfo]
    );

    return { getColSpan: getColSpan, isDataElementVisible: isDataElementVisible };
}
