import React from "react";
import {
    DataTable,
    TableHead,
    DataTableRow,
    TableBody,
    // @ts-ignore
} from "@dhis2/ui";
import { GridViewModel } from "./GridFormViewModel";
import { DataFormInfo } from "./AutogeneratedForm";
import { SectionGrid } from "../../../domain/common/entities/DataForm";
import { DataElementItem } from "./DataElementItem";
import { makeStyles } from "@material-ui/core";
import DataTableSection from "./DataTableSection";
import { CustomDataTableCell, CustomDataTableColumnHeader } from "./datatables/CustomDataTables";
import { DataTableCellFormula } from "./datatables/DataTableCellFormula";
import { DataTableCellRowName } from "./datatables/DataTableCellRowName";
import _ from "lodash";
import { IndicatorItem, RowIndicatorItem } from "../../components/IndicatorItem/IndicatorItem";
import { checkIndicatorDirection } from "../../../domain/common/entities/Indicator";
import { getFilteredIndicators } from "./GridFormViewModel";
import i18n from "../../../locales";

/*
 * Convert data forms into table, using "-" as a separator. An example for section ITNs:
 *
 *    - ITNs - Basic - Written Policy
 *    - ITNs - Basic - Policy Implemented
 *    - ITNs - Extended - Written Policy
 *    - ITNs - Extended - Policy Implemented
 *
 *    This will create this table:
 *
 *    ITNs            | Written Policy | Policy Implemented |
 *    ITNs - Basic    |                |                    |
 *    ITNs - Extended |                |                    |
 **/

export interface GridFormProps {
    dataFormInfo: DataFormInfo;
    section: SectionGrid;
}

const GridForm: React.FC<GridFormProps> = props => {
    const { dataFormInfo, section } = props;
    const grid = React.useMemo(() => GridViewModel.get(section, dataFormInfo, "grid"), [section, dataFormInfo]);
    const classes = useStyles();

    const showIndicatorsAfter = section.indicators.some(indicator => checkIndicatorDirection(indicator, "after"));
    const showIndicatorsBefore = section.indicators.some(indicator => checkIndicatorDirection(indicator, "before"));
    const nonDirectionalIndicators = section.indicators.filter(
        indicator => !checkIndicatorDirection(indicator, "before") && !checkIndicatorDirection(indicator, "after")
    );

    return (
        <DataTableSection section={grid} sectionStyles={props.section.styles} dataFormInfo={dataFormInfo}>
            <DataTable className={classes.table}>
                <TableHead>
                    <DataTableRow>
                        {grid.useIndexes ? (
                            <CustomDataTableColumnHeader
                                backgroundColor={props.section.styles.columns.backgroundColor}
                                width="30px"
                            >
                                <span className={classes.header}>#</span>{" "}
                            </CustomDataTableColumnHeader>
                        ) : (
                            !_.isEmpty(grid.rows) && (
                                <CustomDataTableColumnHeader
                                    backgroundColor={props.section.styles.columns.backgroundColor}
                                    width="800px"
                                ></CustomDataTableColumnHeader>
                            )
                        )}

                        {grid.dataEntryPeriod && (
                            <CustomDataTableColumnHeader
                                backgroundColor={props.section.styles.columns.backgroundColor}
                                width="50px"
                            >
                                {i18n.t("Period")}
                            </CustomDataTableColumnHeader>
                        )}

                        {showIndicatorsBefore && (
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                key="column-indicators-before"
                            ></CustomDataTableColumnHeader>
                        )}

                        {grid.columns.map(column => (
                            <CustomDataTableColumnHeader
                                backgroundColor={props.section.styles.columns.backgroundColor}
                                className={classes.columnWidth}
                                key={`column-${column.name}`}
                            >
                                <div className={classes.header}>
                                    <span>{column.name}</span>
                                    <span
                                        className={classes.description}
                                        dangerouslySetInnerHTML={{ __html: column.description || "" }}
                                    ></span>
                                </div>
                            </CustomDataTableColumnHeader>
                        ))}

                        {showIndicatorsAfter && (
                            <CustomDataTableColumnHeader
                                backgroundColor={section.styles.columns.backgroundColor}
                                key="column-indicators-after"
                            ></CustomDataTableColumnHeader>
                        )}
                    </DataTableRow>
                </TableHead>

                <TableBody>
                    {grid.rows.map((row, idx) => (
                        <DataTableRow key={`policy-${row.name}`}>
                            <CustomDataTableCell backgroundColor={props.section.styles.rows.backgroundColor}>
                                <DataTableCellRowName
                                    html={row.htmlText}
                                    name={grid.useIndexes ? (idx + 1).toString() : row.name}
                                />
                            </CustomDataTableCell>

                            {grid.dataEntryPeriod && (
                                <CustomDataTableCell backgroundColor={props.section.styles.rows.backgroundColor}>
                                    <span>{grid.dataEntryPeriod.label}</span>
                                </CustomDataTableCell>
                            )}

                            {getFilteredIndicators(section.indicators, row, "before").map(
                                indicator =>
                                    indicator && (
                                        <IndicatorItem
                                            key={`${indicator.id}-${row.name}`}
                                            indicator={indicator}
                                            dataFormInfo={dataFormInfo}
                                            periods={[dataFormInfo.period]}
                                        />
                                    )
                            )}

                            {row.items.map((item, idx) =>
                                item.dataElement ? (
                                    <CustomDataTableCell
                                        backgroundColor={props.section.styles.rows.backgroundColor}
                                        key={item.dataElement.id}
                                    >
                                        <DataElementItem
                                            columnTotal={item.columnTotal}
                                            columnDataElements={item.columnDataElements}
                                            manualyDisabled={item.disabled}
                                            noComment={item.disableComments}
                                            dataElement={item.dataElement}
                                            dataFormInfo={dataFormInfo}
                                            rows={grid.rows}
                                            period={grid.dataEntryPeriod?.id || dataFormInfo.period}
                                            lockException={grid.dataEntryPeriod !== undefined}
                                        />
                                    </CustomDataTableCell>
                                ) : (
                                    <CustomDataTableCell
                                        backgroundColor={props.section.styles.rows.backgroundColor}
                                        key={`cell-${idx}`}
                                    ></CustomDataTableCell>
                                )
                            )}

                            {getFilteredIndicators(section.indicators, row, "after").map(
                                indicator =>
                                    indicator && (
                                        <IndicatorItem
                                            key={`${indicator.id}-${row.name}`}
                                            indicator={indicator}
                                            dataFormInfo={dataFormInfo}
                                            periods={[dataFormInfo.period]}
                                        />
                                    )
                            )}
                        </DataTableRow>
                    ))}
                    {grid.summary.map(summary => (
                        <DataTableRow key={`total-custom-row-${summary.cellName}`}>
                            <CustomDataTableCell
                                backgroundColor={props.section.styles.totals.backgroundColor}
                                key={`total-column-${summary.cellName}`}
                            >
                                {summary.cellName}
                            </CustomDataTableCell>
                            {summary.cells.map(itemTotal => {
                                return (
                                    <DataTableCellFormula
                                        key={itemTotal.columnName}
                                        dataFormInfo={dataFormInfo}
                                        styles={props.section.styles}
                                        total={itemTotal}
                                        formula={itemTotal.formula}
                                    />
                                );
                            })}
                        </DataTableRow>
                    ))}

                    {nonDirectionalIndicators.map(indicator => (
                        <RowIndicatorItem
                            key={`parent_${indicator.id}`}
                            indicator={indicator}
                            colSpan="2"
                            dataFormInfo={dataFormInfo}
                            periods={[grid.dataEntryPeriod?.id || dataFormInfo.period]}
                        />
                    ))}
                </TableBody>
            </DataTable>
        </DataTableSection>
    );
};

const useStyles = makeStyles({
    wrapper: { margin: 10 },
    header: {
        fontSize: "1.2em",
        fontWeight: "bold",
        flexDirection: "column",
        textAlign: "center",
        display: "flex",
        padding: "4px",
    },
    description: { fontWeight: "normal", fontSize: "0.8em" },
    table: { borderWidth: "3px !important" },
    columnWidth: { minWidth: "14.25em !important" },
});

export default React.memo(GridForm);
