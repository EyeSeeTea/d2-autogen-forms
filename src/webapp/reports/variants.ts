import { isElementOfUnion, UnionFromValues } from "../../utils/ts-utils";

const reportVariants = ["autogenerated-forms", "autogenerated-forms-configurator"] as const;

export type ReportVariant = UnionFromValues<typeof reportVariants>;

export function getVariant(): ReportVariant {
    const value = import.meta.env.VITE_REPORT_VARIANT || "";
    return isElementOfUnion(value, reportVariants) ? value : reportVariants[0];
}
