import { DataSet } from "../../domain/autogenerated-forms-configurator/entities/DataSet";
import { DataSetRepository } from "../../domain/autogenerated-forms-configurator/repositories/DataSetRepository";
import { D2Api } from "../../types/d2-api";
import { Namespaces } from "../common/clients/storage/Namespaces";

export class DataSetDataStoreRepository implements DataSetRepository {
    constructor(private api: D2Api) {}

    async get(): Promise<DataSet[]> {
        const d2DataSets = await this.getMetadata();
        const autogenDatastoreKeys = await this.api.dataStore(Namespaces.D2_AUTOGEN_FORMS).getKeys().getData();

        return d2DataSets.filter(dataSet => autogenDatastoreKeys.includes(dataSet.code));
    }

    private async getMetadata(): Promise<DataSet[]> {
        const { objects: dataSets } = await this.api.models.dataSets
            .get({
                fields: {
                    name: true,
                    code: true,
                },
                filter: {
                    code: {
                        ne: undefined,
                    },
                },
                paging: false,
            })
            .getData();

        return dataSets;
    }
}
